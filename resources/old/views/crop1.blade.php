                <p><span style="color: #008080;"><strong>(1) Land improvement</strong></span></p>
                <p>Remediate the soil properly. Must be settled. Before plowing, spray the herbicide according to the label,
                    and then plow after weeding. It is important to plow the land deeply and plow it. It should be noted
                    that the rate of germination will be low if there is too much plow left.</p>
                <p><span style="color: #008080;"><strong>(2) Adding soil</strong></span></p>
                <p>Apply NPK compound fertilizer (15:15:15) at the rate of 1 bag per 1 acre before sowing.
                    Add urea (pearl) fertilizer at the rate of 1 bag per 1 acre when the seedlings are 4 inches tall. When
                    the plant reaches 1 foot, urea (pearl) fertilizer should be added at the rate of 1 bag per 1 acre.</p>
                <p><span style="color: #008080;"><strong>(3) Planting (millet/maize)</strong></span>
                </p>
                <p>When soil moisture is sufficient,</p>
                <p>- Corn for cattle feed - 1 acre should be planted at the rate of 5 - 7 kg. For better yield
                    It should be planted in the season of sunshine.</p>
                <p>- 1 acre of maize should be planted at a rate of 7-10 kg. If there is enough water/rain it can be
                    productive at other times.</p>
                <p>After planting, cover with 1 inch of soil. Note that low coverage can affect germination rates. 12 - 13
                    inches between rows and 4 inches between plants.</p>
                <p>If the spacing between the plants is too wide, the corn plants will be too dense, so plant them at least
                    4 inches apart to maintain the size and nutritional value of the plant.</p>
                <p>They sow seeds not in rows, but when they are sown, they tend to be uneven in thickness. By planting in
                    rows, it is easy to maintain and manage and easy to harvest.</p>
                <p><span style="color: #008080;"><strong>(4) Proper harvesting</strong></span></p>
                <p>You can start harvesting when the plant is 70 - 80 days old or when the leaves/buds start to produce
                    milk. (Systematic cultivation varies slightly depending on weather and day length.)</p>
                <p>Don't let the corn plant mature. As the corn plant matures, its nutritional value will decrease.</p>
                <p>You can harvest corn only once. Cows can be fed 3 harvests of corn. When mowing, leave 6 inches from the
                    ground. Only then will the next time produce fast and good yield. (Pearl fertilizer must be fed about 1
                    foot up.)</p>
                <p><span style="color: #008080;"><strong>(5) Water supply</strong></span></p>
                <p>&nbsp; &nbsp; If you can supply water when the soil moisture is low, the yield will be better.
                    By growing fodder corn, raw feed can be obtained and production costs can be reduced.
                    Seasonal planting can produce multiple crops. The excess can be made into pickles and stored.</p>
                <br>
