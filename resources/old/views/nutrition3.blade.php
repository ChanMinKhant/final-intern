                    <div><strong>Questions and answers of food calculation</strong></div>
                    <ul>
                        <li>A lactating cow with a body weight of (550 kg) needs 12 days (12) weeks to give birth and so far
                            produces 6 peittha (9.6 liters) of milk. How much energy will be required in the feed to feed
                            that
                            cow?</li>
                        <li>Amount of energy needed by the body =&nbsp; &nbsp;63 MJME</li>
                        <li>Energy needed to produce 6 peittha of milk = 6 pints of milk&nbsp; x 9.3&nbsp; = 55.8 MJME</li>
                        <li>Energy needed for 12 weeks of pregnancy = 13 MJME</li>
                        <li>Total amount of energy needed for the day = 132 MJME/day</li>
                        <li>This cow was treated with 4 kg of CP 006 feed&nbsp; Starting to feed. So, how much Monbar salt
                            grass
                            do you need to feed for the required energy?&nbsp;</li>
                        <li>CP 006 4 kg&nbsp; x (the amount of energy contained in the food) 9 MJME = 36 MJME</li>
                        <li>Total amount of energy required (132) – 36 = 96 MJME</li>
                        <li>&nbsp;(The amount of energy to be obtained from raw food)</li>
                        <li>96 / 8.5 = 11.3 kg dry amount of Monbar salt grass&nbsp;</li>
                        <li>(full quality&nbsp; amount of raw food)</li>
                        <li>&nbsp;We need to feed 57 kg of wet Monbar salt grass with 20 percent dry matter.</li>
                    </ul> <br>
