                <p> - Mulato II (မိုလတို)မြက်ကို အမြဲတမ်းစားကျက်များတွင် စိုက်ပျိုးနိုင်ပါသည်။
                    ထွက်နှုန်းကောင်း/အရသာကောင်းသောစပ်မျိုးဖြစ်သည်။
                    လွှတ်ကျောင်းခံနိုင်သောမြက်မျိုးဖြစ်သည်။</p>
                <p> -ပဲပန်းနှင့် ရောနှောစိုက်ပျိုးရန်သင့်တော်သည်။ အဆစ်မျိုးပွားခြင်းဖြင့် ပြန့်ပွားနိုင်သည်။</p>
                <p> - ရေဝပ်သောနေရာတွင် စိုက်ပျိုးရန်မသင့်တော်ပေ။</p>
                <p> - နို့စားနွား၊အသားစားနွား အတွက်အထူးကောင်းမွန်သော မြက်မျိုးဖြစ်သည်။</p>
                <p> - ထုတ်လုပ်မှုနှင့်အာဟာရတန်ဖိုးမြင့်များစွာပါဝင်သည်။</p>
                <p> - ခြောက်သွေ့မှုကိုခံနိုင်သည်။ အရိပ်ရအပင်များအောက်တွင် ဖြစ်ထွန်းမှုနည်းပါးသည်။</p>

                <p> - ရိတ်သိမ်းသယ်ယူကျွေးမွေးခြင်း၊ လွတ်ကျောင်းကျွေးမွေးခြင်းစသည့် စားကျက်များတွင်
                    ပဲမျိုးနွယ်ဝင်စားကျက်ပင်များနှင့် (၁၆)လက်မခြား၍စိုက်ပျိုးနိုင်သည်။</p>

                <p> - (၂၀-၃၀)ရက်ခြားရိတ်သိမ်းသယ်ယူကျွေးမွေးနိုင်သည်။ မျိုးစေ့ထုတ်ရန်ခက်ခဲသည်။</p>
