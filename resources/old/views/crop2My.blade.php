                    မြေလျှောက်အဆစ်ကျအပင်ပွားနှုန်းကောင်းသည်။ </p>
                <p> - မြေသြဇာအသင့်တင့်ရှိသော မြေများတွင်ဖြစ်ထွန်းပြီး နှစ်ရှည်ခံပင်ဖြစ်သည်။
                    တစ်ဧကလျှင် မျိုးစေ့ (၂)ကီလိုဂရမ်စိုက်ပျိုးရန်လိုသည်။ </p>
                <p> - အရိပ်အောက်တွင် စိုက်ပျိုးနိုင်သည်။ သရက်စိုက်ခင်းများအောက်တွင် စိုက်ပျိုးနိုင်သည်။
                    ရေဝပ်သောနေရာများတွင် စိုက်ပျိုးရန်မသင့်ပေ။</p>
                <p> - ရိတ်သိမ်းသယ်ယူကျွေးမွေးခြင်း၊ လွတ်ကျောင်းကျွေးမွေးခြင်း အသုံးပြုနိုင်သည်။
                    အခြားစားကျက်ပဲမျိုး၊ စားကျက်မြက်မျိုးများနှင့် ရောနှောစိုက်ပျိုးနိုင်သည်။
                    စားကျက်ပင်များကို (၁၆)လက်မခြား၍ ရောနှောစိုက်ပျိုးနိုင်သည်။
                    စိုက်ပျိုးပြီး မြက်ပင်များကို (၃-၄)ပတ်ခြား၍ ကျွေးမွေးနိုင်သည်။ </p>
