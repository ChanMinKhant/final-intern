                <p class="text-center"><b>Desmodiumintortum cv Greenleaf “Greenleaf Desmodium”</b></p>
                <p> - Greenleaf Desmodium(ဒက်စမိုဒီယန်း)သည် နှစ်ရှည်ပဲမျိုးနွယ် စားကျက်ပင်ဖြစ်သည်။ </p>
                <p> - မြေပြင်ပြန့်ကျဲ ပေါက်ရောက်သောအပင်ဖြစ်သည်။ တောင်ပေါ် အအေးပိုင်းဒေသများတွင် စိုက်ပျိုး နိုင်သည်။
                    ရေရရှိနိုင်သောဒေသများတွင် ပိုမိုဖြစ်ထွန်းနိုင်သည်။ </p>
                <p> - ခြောက်သွေ့မှုကိုလည်းခံနိုင်သည်။ အရိပ်ရအပင်များအောက်တွင် စိုက်ပျိုးနိုင်သည်။
                    စားကျက်မြက်ပင်များနှင့် ရောနှောစိုက်ပျိုးနိုင်သည်။</p>
                <p> - လွှတ်ကျောင်း စားကျက်များတွင် လွှတ်ကျောင်း အသင့်တင့်ခံနိုင်သည်။ အရသာရှိမှုနှင့်
                    အာဟာရဓာတ်ပါဝင်မှုအသင့်တင့်ဖြစ်သည်။
                    တောင်ပေါ်ဒေသများတွင် မျိုးစေ့ထုတ်လုပ်ရန် အထူးကောင်းမွန်သည်။</p>
                <p> - စိုက်ပျိုးရာသီတွင် အဆစ်ဖြတ်မျိုးပွားနိုင်သည်။ </p>
                <p> - တစ်ဧကမျိုးစေ့ထွက်နှုန်း (၃၂-၄၀)ကီလိုရှိသည်။ </p>
                <p> - တစ်ဧက၏အစာခြောက်ပမာဏထွက်ရှိမှုသည် (၆-၇)တန်ဖြစ်သည်။</p> <br><br>

