            <div class="page-content">
                <p>- ကိုယ်ဝန်ဆောင်ပြီး နို့နားထားသည့်အခြေအနေမှ နွားကလေးမွေးပြီး နို့ပေးသည့်အခြေအနေသို့
                    ပြောင်းလဲသည့်ကာလ ဖြစ်သည်။ မမွေးခင် (၃)ပတ်အလိုနှင့် မွေးပြီး (၃)ပတ်အကြာ ကြားကာလကို အကူးအပြောင်းကာလဟု
                    သတ်မှတ်သည်။</p>
                <p>- ထိုကာလသည် နွားမမှ အာဟာရဗေဒဆိုင်ရာနှင့် ကိုယ်တွင်း ဟိုမုန်းထုတ်လုပ်မှုဆိုင်ရာ
                    အပြောင်းအလဲမြန်မှုများကို ထိန်းညှိလုပ်ဆောင်နေရသော ကာလဖြစ်သည်။</p>
                <p>- နွားကလေးမွေးသည့်ကာလ ပတ်လည်တွင် အစာစားနှုန်း(dry matter intake)လျော့ကျခြင်း၊ နို့ဦးရည်နှင့်
                    နို့ထုတ်လုပ်ရန်အတွက် အာဟာရဓာတ်ပိုမိုလိုအပ်လာခြင်း
                    စသည်တို့သည် ဤကာလအတွင်း အရေးအကြီးဆုံးသော ဇီဝကမ္မဆိုင်ရာပြောင်းလဲမှုများ ဖြစ်သည်။</p>
                <p><span style="color: #008080;"><strong> *** အကူးအပြောင်းကာလသည်
                            ဘာကြောင့်အရေးကြီးတာလဲ?</strong></span></p>
                <p>- ကိုယ်ဝန်ဆောင်နို့နားကာလမှ နို့ပေးကာလသို့ ကောင်းမွန်စွာကူးပြောင်းနိုင်သည့်
                    နွားမများသည် ကောင်းမွန်သော ကျန်းမာရေး၊ ထုတ်လုပ်မှုနှင့် မျိုးပွားခြင်းများ ရရှိမှာ ဖြစ်ပါတယ်။
                </p>
                <p>- <span lang="hi-IN">အကယ်၍ အဆင်ပြေစွာ မကူးပြောင်းနိုင်လျှင် နို့ထွက်နှုန်းကျခြင်း၊
                        ကိုယ်ခံအားစနစ်လျော့ကျခြင်း၊ မျိုးပွားမှုဆိုင်ရာ လုပ်ဆောင်ချက်များ လျော့ကျခြင်း စသည်တို့ဖြစ်စေပြီး
                        စီးပွားရေးအရ ဆုံးရှုံးမှုများစေပါသည်။</span></p>
                <p>- <span lang="hi-IN">ကိုယ်ဝန်ဆောင်ကာလ နောက်ဆုံးအပတ်အတွင်းတွင် သန္ဓေသားနှင့် အချင်းတိုင်သည်
                        စွမ်းအင်ဓာတ်အများဆုံး လိုအပ်နေသည့်အခြေအနေဖြစ်ပြီး နွားကလေးမွေးပြီးချိန်တွင်လည်း
                        နို့ဖြစ်တည်စေရန်နှင့် နို့ထုတ်လုပ်မှုမြှင့်တင်ရန် စွမ်းအင်ဓာတ်(energy) များစွာ
                        လိုအပ်နေချိန်ဖြစ်သည်။</span></p>
                <p>- <span lang="hi-IN">သို့သော် ကိုယ်ဝန်ဆောင်ချိန်၊ မွေးဖွားချိန်၊ နို့ပေးချိန် စသည့်အကူးအပြောင်းကာလတွင်
                        ဟိုမုန်းဓာတ်များ၊ ရုပ်ပိုင်း စိတ်ပိုင်းဆိုင်ရာပြောင်းလဲမှုများနှင့်အတူ ဖိစီးမှုများကြောင့်
                        အစာစားနှုန်းကျပြီး အစာမှ စွမ်းအင်ဓာတ်(energy)ရရှိနိုင်မှုလည်း
                        လျှော့နည်းသွားသည်။</span></p>
                <p>*** <span lang="hi-IN">နွားမများတွင် စွမ်းအင်ဓာတ်ရရှိမှု(energy supply)သည်
                        စွမ်းအင်ဓာတ်လိုအပ်ချက်(energy demand)ထက် နည်းနေသည့်
                        အခြေအနေကို စွမ်းအင်ဓာတ်မလုံလောက်ခြင်း(Negative Energy Balance)ဟုခေါ်သည်။</span></p>
                <p>- <span lang="hi-IN">စွမ်းအင်ဓာတ်မလုံလောက်သည့်အခါ နွားမ၏ ခန္ဓာကိုယ်တွင်း ထိန်းသိမ်းထားသော
                        ကိုယ်တွင်းအဆီဓာတ်(body fat)များကို ဖြိုခွဲ သုံးစွဲပြီး လိုအပ်တဲ့
                        စွမ်းအင်ဓာတ်ရရှိအောင် ပြုလုပ်ပါသည်။ ထို့ကြောင့် ခန္ဓာကိုယ်အလေးချိန်နှင့် ခန္ဓာကိုယ်အခြေအနေ
                        ကျဆင်းလာပါသည်။ ထို့ပြင် အဆီဓာတ်များကို စွမ်းအင်အဖြစ် ပြောင်းလဲသုံးစွဲရာတွင် အသုံးပြုမှုထက်
                        ထုတ်လုပ်မှုကများသောအခါ ဘေးထွက်ပစ္စည်းဖြစ်သည့်Beta-Hydroxy Butyric Acid(ketone body)များ
                        သွေးအတွင်းများလာပြီး Ketosis(ကီတိုးဆစ်)
                        ဖြစ်စေပါသည်။</span>
                </p>
                <p>- <span lang="hi-IN">စွမ်းအင်ဓာတ်မလုံလောက်မှုကြောင့် အခြားသော ဆက်စပ်ရောဂါများလည်း ဖြစ်ပွားနိုင်ပါသည်။
                        ထို့ပြင် ဟိုမုန်းထွက်ရှိမှုမညီမျှခြင်းနှင့် ကိုယ်ခံအားစနစ်လျော့ကျခြင်းများ ဖြစ်တတ်ပါသည်။</span></p>
                <p>- <span lang="hi-IN">မမွေးခင် ၂ ပတ်နှင့် မွေးပြီး ၂ ပတ်အတွင်းသည် ကိုယ်ခံအားစနစ်ပိုမို
                        အားနည်းနိုင်သည့်ကာလဖြစ်သည်။ ထိုအချိန်တွင် နို့အုံရောင်ရောဂါ(mastitis)၊
                        သားအိမ်ရောင်ရောဂါ(metritis)နှင့် အချင်းကျန်ခြင်း(retained
                        placenta)စသည်တို့ ဖြစ်ပွားနိုင်ပါသည်။ အခြားသော ကူးစက်ရောဂါများ ဖြစ်နိုင်ရန်
                        အခွင့်အလမ်းလည်းဖြစ်စေသည်။</span></p>
                <p>- <span lang="hi-IN">အာဟာရဓာတ်မလုံလောက်မှုနှင့်အတူ ဟိုမုန်းထွက်ရှိမှု မညီမျှမှုတို့ကြောင့်
                        သားတောင်းနောက်ကျခြင်း၊ မျိုးဥကြွေမှုနောက်ကျခြင်း၊ သားအောင်နှုန်းနည်းခြင်း စသည်တို့ကြောင့်
                        တစ်သားနှင့်တစ်သားကြား ကြားကာလကြာပြီး တစ်နှစ်တစ်သားမရနိုင်သည့် ပြဿနာလည်းဖြစ်လာပါသည်။</span></p>
                <p><span style="color: #008080;"><strong> ***ကျန်းမာရေး ယိုယွင်းမှုဆိုင်ရာ
                            ပြဿနာများ***</strong></span></p>
                <p>(<span lang="hi-IN">၁</span>)စွမ်းအင်ဓာတ်ယိုယွင်းမှုဆိုင်ရာ
                    ပြဿနာများ(Energy-Related Disorders)</p>
                <p> <span lang="hi-IN">
                        - အစာအိမ်အတွင်း အက်စစ်ဓာတ်များခြင်း/ခြေလက်နာခြင်း
                        (Rumen acidosis/ Lameness)<br>
                        - ကီတိုးဆစ်(Ketosis)<br>
                        - အသည်းအဆီဖုံးခြင်း(Fatty liver syndrome)<br>
                        - Displaced abomasum </span></p>
                <p>(<span lang="hi-IN">၂</span>) <span lang="hi-IN">သတ္တုဓာတ်</span>-<span lang="hi-IN">ဗီတာမင်ဓာတ်
                        ယိုယွင်းမှုဆိုင်ရာပြဿနာများ&nbsp;</span>(Mineral-Vitamin Related Disorders)</p>
                <p>- Milk fever<br> - Udder edema</p>
                <p>(<span lang="hi-IN">၃</span>) <span lang="hi-IN">ကိုယ်ခံအားစနစ် ယိုယွင်းမှုဆိုင်ရာ
                        ပြဿနာများ&nbsp;</span>(Immune System Related Disorders)</p>
                <p><span lang="hi-IN">
                    - အချင်းကျန်ခြင်း(Retained placenta)<br>
                    - သားအိမ်ရောင်ရမ်းခြင်း(Metritis)<br>
                    - နို့အုံရောင်ရောဂါ(Mastitis)
                </span></p>
                <p>*** <span lang="hi-IN">အကူးအပြောင်းကာလအတွင်း ကောင်းမွန်သည့် အာဟာရနှင့် အစာကျွေးစနစ် စီမံမှုသည်
                        နွားကလေးမွေးပြီးနောက် အစာစားနှုန်း(dry matter intake)တိုးလာစေရာတွင်
                        အထောက်အကူဖြစ်ခြင်း၊ မွေးဖွားရာတွင် လွယ်ကူစေခြင်း၊ နွားကျန်းမာရေး၊ နို့ထုတ်လုပ်မှု နှင့်
                        မျိုးပွားမှုဆိုင်ရာ လုပ်ဆောင်ချက်များ ကောင်းမွန်စေပါသည်။</span></p>
