    <h3 class="page-title mt-3" style="padding-top: 25px; margin-bottom: 40px;">
        <strong>Ketosis</strong>
    </h3>

    <div class="page-cover d-flex justify-content-center align-items-center"
        style="max-height: 400px; overflow: hidden; margin-bottom:30px;">
        <img src="https://greenway.sgp1.digitaloceanspaces.com/post_covers/0349adca6776cf2b486e64a0c0c8ff02.jpg"
            class="page-cover img-fluid" style="max-height: 400px;">
