    <div class="container">
        <div class="page-body">
            <p>- Ketosis is seen in dairy cows in a state of negative energy balance and is associated with increased
                glucose demand from the animal. Cow with high audience production; It is common in all cattle that eat poor,
                low-nutrition food.Symptoms can be seen before birth; It is most noticeable in the first month after birth
                and can also be seen in the second month.
            </p>
            <p><strong><span style="color: #993300;">* Cause of occurrence</span></strong></p>
            <p>- During the last week of pregnancy, the fetus and placenta require the most energy, and when the calf is
                born, it needs a lot of energy to make milk and increase milk production. However, heifers have high
                nutritional requirements at the time of birth and often have a low feed rate.
            </p>
            <p><span style="color: #993300;">* A condition in which energy supply is less than energy demand in cows is
                    called negative energy balance.
                </span>
            </p>
            <p>- In such a situation where energy is insufficient, the problem is that the amount of glucose in the blood
                and body tissue (body tissue) decreases.
            </p>
            <p>- When the energy is insufficient, the body fat stored in the heifer's body is broken down and used to obtain
                the
                necessary energy. As a result, body weight and body condition decrease. In addition, when the production is
                more than the consumption of fats into energy, the side product Beta-Hydroxy Butyric Acid (ketone body)
                increases in the blood and causes ketosis.
            </p>
            <p><strong><span style="color: #993300;">* Symptoms</span></strong></p>
            <p><span style="color: #993300;">(1) Wasting form</span></p>
            <p>- Decrease in feed rate, weight loss, and milk yield.</p>
            <p>- Staggering without energy not being able to stand up properly; Standing head</p>
            <p>- Smell of acetone every time you breathe in milk (sweet smell)</p>
            <p>This stage is not fatal, but recovery will be slow if left untreated. The milk production rate will gradually
                rise again after more than a month, but the original production level cannot be fully restored.
            </p>
            <p><span style="color: #993300;">(2) Nervous form</span></p>
            <p>- aggression Tongue-to-mouth Walking around in a circle</p>
            <p>- Loud shouting for no reason</p>
            <p><strong><span style="color: #993300;">* Treatment</span></strong></p>
            <p>
                The main purpose is to replenish glucose in the body. Glucose supplement needs to be done quickly. After
                that, glucose must be provided long-term with continued treatment. Get veterinary treatment/advice.
            </p>
            <p><span style="color: #993300;">(1) Glucose replacement</span></p>
            <p>- Dextrose solution (IV) (It is useful as a quick treatment)</p>
            <p>- Propylene glycol (or) glycerine (It only has a long-term effect·Åä It should be continued for 2-4 days)</p>
            <p>- Multivitamin injection</p>
            <p><span style="color: #993300;">(2) Hormonal therapy</span></p>
            <p>- Corticosteroids</p>
            <p><strong>(Veterinarians need to pay themselves.)</strong></p>
            <p>- Corticosteroids break down protein in the muscle and convert it into glucose, which can help lower blood
                glucose levels.</p>
            <p>- The important thing is not to break down too much protein in the muscle. Therefore, when using
                corticosteroids, starch-rich foods or propylene glycol should be given.
            </p>
            <p><strong><span style="color: #993300;">* Prevention</span></strong></p>
            <p>- It depends on the ability to feed and manage enough food.</p>
            <p>- During breastfeeding and near birth, the body should be in a state that is not too lean and not too fat.
            </p>
            <p><strong>(Do not become overweight)</strong></p>
            <p>- Approximately 2 weeks prior to calving, a good quality roughage/green forage should be fed to meet the
                energy requirements of the cow. Cereal grains can be fed.
            </p>
            <p>- After birth, in the early lactation period, you must prepare food that is full of nutrients (meat, energy).
            </p>
            <p>- Propylene glycol can be added to food.</p>
            <p><strong><span style="color: #993300;">*** If the cow can still get up -</span></strong></p>
            <p>Special attention must be paid to feeding. Foods that are high in energy should be fed.</p>
            <p><strong><span style="color: #993300;">*** If the cow can't get up -</span></strong></p>
            <p>- Keep it in a good location in the shade. To avoid physical pain, the body must be adjusted regularly from
                one side to the other.
            </p>
            <p>- Provide water and good quality food. Propylene glycol can be given.</p>
            <p>- If the cow cannot get up completely on its own, you must use a hip-lifter (an object that holds the cow by
                the hips) or other means to help it stand. (Doing so must be done systematically.)
            </p>
            <p><strong>Source - MDEP</strong></p>

