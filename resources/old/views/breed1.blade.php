            <p style="padding-top: 20px;"> If your heifer is thin and showing no signs of bloat, the main
                reason
                is that she is not eating enough feed or she is not getting enough nutrients in her feed.</p>
            <p> Although energy is fully sufficient for cows, water, Nutrients and minerals also need to be
                met.</p>
            <p> According to Myanmar's farming tradition, dairy cattle farming is facing the problem of not
                drinking enough water. So clean water should be arranged so that you can drink it as you please.</p>
            <p> Hay-fed cows may be malnourished.
                High-quality raw food contains moderate amounts of protein and fat. Small foods obtained from various beans,
                such as peanuts, Chickpea flour etc. contains many nutrients.</p>
            <p> Sometimes lactating cows may also have mineral needs. They are less important than other
                factors affecting reproduction.
                If you encounter problems with mineral requirements, metal blocks, metal injections; Calcium and Phosphorus
                (DCP) can be added to feed.
                Tell the nearest veterinarian about the condition of your cow and get the appropriate treatment. Actions
                should be taken.</p>
