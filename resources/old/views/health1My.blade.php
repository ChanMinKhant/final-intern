        <div class="page-body">
            <p>- သကြားဓာတ် ချို့တဲ့ခြင်း (Ketosis) သည် စွမ်းအင်ဓာတ်မညီမျှခြင်း (Negative Energy Balance) အခြေအနေတွင်ရှိသော
                နို့စားနွားမများတွင်
                တွေ့ရပြီး တိရစ္ဆာန်မှ glucose လိုအပ်ချက်မြင့်လာတာနှင့် ဆက်စပ်ဖြစ်ပွားသည်။ အထူးသဖြင့်
                ထုတ်လုပ်မှုမြင့်သောနွား၊ အရည်အသွေးမကောင်း အာဟာရနည်းသောအစာကို စားရသည့်နွားများတွင် အဖြစ်များသည်။
                ရောဂါလက္ခဏာများကို မမွေးခင်တွင်မြင်တွေ့နိုင်ပါတယ်။ ဒါပေမယ့် မွေးပြီးပထမလတွင် အသိသာဆုံးဖြစ်ပြီး
                ဒုတိယလတွင်လည်း တွေ့နိုင်သည်။</p>
            <p><strong><span style="color: #993300;">* ဖြစ်ပွားရသည့်အကြောင်းအရင်း</span></strong></p>
            <p>- ကိုယ်ဝန်ဆောင်ကာလ နောက်ဆုံးအပတ်အတွင်းတွင် သန္ဓေသားနှင့် အချင်းတိုင်သည် စွမ်းအင်ဓာတ်အများဆုံးလိုအပ်နေသည့်
                အခြေအနေဖြစ်ပြီး နွားကလေးမွေးပြီးချိန်တွင်လည်း နို့ဖြစ်တည်စေရန်နှင့် နို့ထုတ်လုပ်မှုမြှင့်တင်ရန် စွမ်းအင်ဓာတ်
                (energy) များစွာလိုအပ်နေချိန်ဖြစ်သည်။ သို့သော် နွားမများသည် မွေးပြီးစအချိန်တွင် အာဟာရလိုအပ်ချက်ကများပြီး
                အစာစားနှုန်းနည်းနေတတ်သည်။</p>
            <p><span style="color: #993300;">*** နွားမများတွင် စွမ်းအင်ဓာတ်ရရှိမှု (energy supply) သည်
                    စွမ်းအင်ဓာတ်လိုအပ်ချက်
                    (energy demand) ထက် နည်းနေသည့် အခြေအနေကို စွမ်းအင်ဓာတ်မလုံလောက်ခြင်း (Negative Energy Balance)
                    ဟုခေါ်သည်။</span></p>
            <p>- ထိုသို့ စွမ်းအင်ဓာတ်မလုံလောက်သည့်အခြေအနေတွင် သွေးနှင့် ခန္ဓာကိုယ်တစ်ရှူး (body tissue)များတွင် glucose ပမာဏ
                လျော့နည်းခြင်း ပြဿနာဖြစ်လာသည်။</p>
            <p>- စွမ်းအင်ဓာတ်မလုံလောက်သည့်အခါ နွားမ၏ ခန္ဓာကိုယ်တွင်း ထိန်းသိမ်းထားသော ကိုယ်တွင်းအဆီဓာတ် (body fat) များကို
                ဖြိုခွဲသုံးစွဲပြီး လိုအပ်တဲ့ စွမ်းအင်ဓာတ်ရရှိအောင်ပြုလုပ်ပါသည်။ ထို့ကြောင့် ခန္ဓာကိုယ်အလေးချိန်နှင့်
                ခန္ဓာကိုယ်အခြေအနေ ကျဆင်းလာပါသည်။ ထို့ပြင် အဆီဓာတ်များကို စွမ်းအင်အဖြစ်ပြောင်းလဲသုံးစွဲရာတွင် အသုံးပြုမှုထက်
                ထုတ်လုပ်မှုကများသောအခါ ဘေးထွက်ပစ္စည်းဖြစ်သည့် Beta-Hydroxy Butyric Acid (ketone body) များ
                သွေးအတွင်းများလာပြီး သကြားဓာတ် ချို့တဲ့ခြင်း (Ketosis) ဖြစ်စေပါသည်။</p>
            <p><strong><span style="color: #993300;">* ရောဂါလက္ခဏာများ</span></strong></p>
            <p><span style="color: #993300;">(1) Wasting form</span></p>
            <p>- အစာစားနှုန်းကျခြင်း၊ ခန္ဓာကိုယ်အလေးချိန်ကျခြင်း၊ နို့ထွက်နှုန်းကျခြင်း</p>
            <p>- အားအင်မရှိ ယိမ်းထိုးသွားနေခြင်း၊ ကောင်းစွာမတ်တတ်မရပ်နိုင်ခြင်း၊ ခေါင်းစိုက်နေခြင်း</p>
            <p>- နို့ထဲနှင့် အသက်ရှုလိုက်တိုင်း acetone အနံရခြင်း (ချိုအီအီအနံရခြင်း)</p>
            <p>ဤအဆင့်သည် သေလောက်အောင်ထိ မပြင်းထန်သော်လည်း ကုသမှုမှ မပေးလျှင် ပြန်လည်ကျန်းမာလာရန်နှေးမည်။ နို့ထွက်နှုန်းသည်
                တစ်လကျော်ခန့်ကြာလျှင် တဖြည်းဖြည်း ပြန်မြင့်လာသော်လည်း မူလထုတ်လုပ်မှု level ကိုတော့ အပြည့်အဝပြန်မရနိုင်ပေ။
            </p>
            <p><span style="color: #993300;">(2) Nervous form</span></p>
            <p>- ရန်လိုခြင်း၊ လျှာနှင့်တွေ့ကရာလျက်ခြင်း၊ စက်ဝိုင်းပုံလှည့်ပတ် လျှောက်နေခြင်း</p>
            <p>- အကြောင်းအရင်းမရှိ ကျယ်လောင်စွာအော်နေခြင်း</p>
            <p><strong><span style="color: #993300;">* ကုသမှု</span></strong></p>
            <p>အဓိကရည်ရွယ်ချက်သည် ခန္ဓာကိုယ်အတွင်း glucose ကိုပြန်လည်ဖြည့်တင်းပေးဖို့ဖြစ်ပါတယ်။ Glucose supplement
                ကိုအမြန်လုပ်ရန်လိုအပ်ပါတယ်။ ထို့နောက် ဆက်လက်ကုသမှုများဖြင့် glucose ကိုရေရှည်ထောက်ပံ့ပေးရပါမယ်။
                တိရစ္ဆာန်ဆေးကုဆရာဝန်များ၏ ကုသမှု/အကြံပေးမှုများ ရယူပါ။</p>
            <p><span style="color: #993300;">(1) Glucose replacement</span></p>
            <p>- Dextrose solution (IV) (လက်တလော အမြန်ကုသမှုအနေနဲ့အကျိုးရှိ)</p>
            <p>- Propylene glycol (သို့) glycerine (ရေရှည်အကျိုးသက်ရောက်မှုရှိ၊ ၂ - ၄ ရက်ကြာအောင်ဆက်ပေးရမည်)</p>
            <p>- Multivitamin injection</p>
            <p><span style="color: #993300;">(2) Hormonal therapy</span></p>
            <p>- Corticosteroids</p>
            <p><strong>(တိရစ္ဆာန်ဆေးကု ဆရာဝန်များကိုယ်တိုင် ပေးဖို့လိုအပ်ပါသည်)</strong></p>
            <p>- Corticosteroids သည် ကြွက်သားအတွင်းမှ protein (အသားဓာတ်)ကို ဖြိုခွဲကာ glucose အဖြစ်ပြောင်းလဲပေးပြီး
                သွေးတွင်း glucose level ကျမှုကို ဖြည့်တင်းပေးနိုင်သည်။</p>
            <p>- အရေးကြီးသောအချက်မှာ ကြွက်သားအတွင်းရှိ protein များ အလွန်အကျွံ ပြိုကွဲခြင်းမဖြစ်စေရန် ဖြစ်သည်။ ထို့ကြောင့်
                Corticosteroids သုံးသည့်အခါ ကစီဓာတ်ကြွယ်ဝသောအစာများ (သို့) propylene glycol ပေးထားဖို့လိုအပ်ပါသည်။</p>
            <p><strong><span style="color: #993300;">* ကာကွယ်ခြင်း</span></strong></p>
            <p>- အစာအလုံအလောက်ကျွေးခြင်းနှင့် စီမံခန့်ခွဲနိုင်မှုပေါ်မူတည်သည်။</p>
            <p>- နို့နားကာလနှင့် မွေးကာနီးအချိန်တွင် ခန္ဓာကိုယ်သည် မပိန်လွန်းမဝလွန်းသော အခြေအနေတွင်ရှိရမည်။</p>
            <p><strong>(အဝလွန်ခြင်းမဖြစ်စေရ)</strong></p>
            <p>- မမွေးခင် ၂ ပတ်ခန့်အလိုတွင် အရည်အသွေးကောင်းတဲ့ အစာကြမ်း/အစာစိမ်းကို အခြေခံပြီး နွား၏စွမ်းအင်လိုအပ်ချက်နှင့်
                ကိုက်ညီအောင်ကျွေးရမည်။ အစေ့အဆံ (cereal grain) များကျွေးနိုင်ပါတယ်။
            </p>
            <p>- မွေးပြီးနောက် နို့ပေးကာလအစောပိုင်းတွင် အာဟာရပြည့်ဝသည့် (အသားဓာတ်၊စွမ်းအင်ဓာတ်) အစာများကျွေးနိုင်အောင်
                ပြင်ဆင်ထားရမည်။
            </p>
            <p>- propylene glycol ကို အစာတွင်ဖြည့်စွက်ကျွေးနိုင်သည်။</p>
            <p><strong><span style="color: #993300;">*** နွားကထနိုင်သေးလျှင်-</span></strong></p>
            <p>အစာကျွေးမှုကို အထူးဂရုစိုက်ရမည်။ စွမ်းအင်ဓာတ်များသောအစာများကျွေးရမည်။</p>
            <p><strong><span style="color: #993300;">*** နွားကမထနိုင်လျှင်-</span></strong></p>
            <p>- အရိပ်ထဲတွင် အခင်းနေရာကောင်းမွန်စွာဖြင့်ထားပါ။ ကိုယ်လက်နာကျင်ကိုက်ခဲမှုမဖြစ်စေရန်
                ခန္ဓာကိုယ်ကိုတစ်ဖက်မှတစ်ဖက်သို့ ပုံမှန်ပြုပြင်ပေးရပါမယ်။
            </p>
            <p>- ရေနှင့်အရည်အသွေးကောင်းသော အစာများထားထားပေးပါ။ Propylene glycol ပေးနိုင်သည်။</p>
            <p>- နွားက သူ့ဘာသာ လုံးဝမထနိုင်လျှင် hip-lifter (နွားကိုတင်ပါးဆုံရိုးမှထိန်း၍ မသည့်အရာ) သို့မဟုတ်
                တခြားအရာများသုံး၍ ရပ်နိုင်အောင် ကူညီပေးရမည်။ (ထိုသို့ပြုလုပ်ရာတွင် စနစ်တကျလုပ်ဆောင်ရမည်။)
            </p>
