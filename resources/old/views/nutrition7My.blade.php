                <p>• ၉၆ / ၈.၅&nbsp; =&nbsp; ၁၁.၃ ကီလိုဂရမ် (အခြောက်အလေးချိန်) မွန်ဘားစားမြက် (အရည်သွေးပြည့် အစာကြမ်းပမာဏ)
                </p>
                <p>• မွန်ဘားစားမြက် အခြောက်ပမာဏ (၂၀%) ရှိသော အစာ၏ အစိုအလေးချိန်ပမာဏ ၁၁.၃ ကီလိုဂရမ်/၂၀ ရာခိုင်နူန်း =
                    မွန်ဘားစားမြက် ၅၇ ကီလိုဂရမ်</p>
                <p><strong>နို့ပေးနွားမအတွက် စွမ်းအင်နှင့် အစာလိုအပ်ချက်</strong></p>
                <p>ခန္ဓာကိုယ် အလေးချိန် (၅၅၀) ရှိသော နို့ပေးနွားမသည် မွေးဖွားရန် ရက်သတ္တပတ်ပေါင်း (၁၂) ပတ် လိုအပ်လျက်ရှိပြီး
                    ယခုအချိန်ထိ နွားနို့ ၁၀ ပိဿာ (သို့) ၁၆ လီတာ ထွက်ရှိသည်။ ထို နွားမကို ကျွေးရန်အစာတွင် စွမ်းအင် မည်မျှ
                    လိုအပ်မည်နည်း။&nbsp;</p>
                <p>• ခန္ဓာကိုယ်ရှင်သန်ရန် အမှန်တကယ်လိုအပ်သော စွမ်းအင် ပမာဏ (Maintenance ) =&nbsp;&nbsp; ၆၃ MJME</p>
                <p>• နို့ (၁၀) ပိသာ ထွက်ရှိရန် လိုအပ်သော စွမ်းအင်ပမာဏ= နို့ ၁၀ ပိဿာ&nbsp; x&nbsp; ၉.၃ = ၉၃ MJME</p>
                <p>• ကိုယ်ဝန်ဆောင်ကာလ (၁၂) ပတ်တွက် လိုအပ်သော စွမ်းအင်ပမာဏ =&nbsp; ၁၃ MJME</p>
                <p>စုစုပေါင်း လိုအပ်သော စွမ်းအင်ပမာဏ = ၁၆၉ MJME/ရက်</p>
                <p>၎င်းနွားမကို CP ၀၀၆ စပ်စာ ၆ ကီလိုဂရမ်&nbsp; စတင်ကျွေးမွေးနေ ပါသည်။ သို့ဖြစ်ပါ၍ လိုအပ်သော စွမ်းအင် အတွက်
                    Mombasa မြက် မည်မျှ ကျွေးမွေးရန် လိုအပ်ပါသနည်း။ မွန်ဘားစားမြက်တွင်ပါဝင်သော အသားဓာတ်ပမာဏ (၈.၅
                    ရာခိုင်နှုန်း)၊ အစိုဓာတ် (၂၀ ရာခိုင်နှုန်း)</p>
                <p>• CP ၀၀၆- ၆ ကီလိုဂရမ် x (အစာနုတွင် ပါဝင်သော စွမ်းအင်ပမာဏ) ၉ MJME = ၅၄ MJME&nbsp;</p>
                <p>• စုစုပေါင်းလိုအပ်သော စွမ်းအင်ပမာဏ (၁၆၉) – ၅၄= ၁၁၅ MJME (အစာကြမ်းမှ ရရှိရမည့်စွမ်းအင်ပမာဏ)</p>
                <p>• ၁၁၅/ ၈.၅ = ၁၃.၅ ကီလိုဂရမ် မွန်ဘားစားမြက် အခြောက်ပမာဏ (အရည်သွေးပြည့် အစာကြမ်းပမာဏ)</p>
                <p>• မွန်ဘားစားမြက် အခြောက်ပမာဏ (၂၀%) ရှိသော အစာ၏ အစိုအလေးချိန်ပမာဏ - ၁၃.၅ ကီလိုဂရမ်/၂၀ ရာခိုင်နူန်း =
                    မွန်ဘားစားမြက် ၆၇.၅ ကီလိုဂရမ်</p>
                <p><strong>နို့ခမ်းနွားမအတွက် စွမ်းအင်နှင့် အစာလိုအပ်ချက်</strong></p>
                <p>ခန္ဓာကိုယ် အလေးချိန် (၅၅၀) ရှိသော နို့ပေးနွားမသည် မွေးဖွားရန် ရက်ပေါင်း (၂) ပတ် လိုအပ်လျက်ရှိသော
                    ထိုနို့နားနွားမ (dry cow) ကို ကျွေးရန်အစာတွင် စွမ်းအင် မည်မျှ လိုအပ်မည်နည်း။</p>
                <p>• န္ဓာကိုယ်ရှင်သန်ရန် အမှန်တကယ်လိုအပ်သော စွမ်းအင် ပမာဏ (Maintenance ) =&nbsp;&nbsp; ၆၃ MJME</p>
                <p>• ကိုယ်ဝန်ဆောင်ကာလ (၂) ပတ်တွက် လိုအပ်သော စွမ်းအင်ပမာဏ = ၅၄ MJME</p>
                <p>စုစုပေါင်း လိုအပ်သော စွမ်းအင်ပမာဏ = ၁၁၇ MJME/ရက်</p>
                <p>၎င်းနွားမကို CP ၀၀၆ စပ်စာ ၁ ကီလိုဂရမ် စတင်ကျွေးမွေးနေ ပါသည်။ သို့ဖြစ်ပါ၍ လိုအပ်သော စွမ်းအင် အတွက်
                    မွန်ဘားစားမြက် မည်မျှ ကျွေးမွေးရန် လိုအပ်ပါသနည်း။ မွန်ဘားစားမြက်တွင်ပါဝင်သော အသားဓာတ်ပမာဏ (၈.၅
                    ရာခိုင်နှုန်း)၊ အစိုဓာတ် (၂၀ ရာခိုင်နှုန်း)</p>
                <p>• CP ၀၀၆ - ၁ ကီလိုဂရမ် x (အစာနုတွင်ပါသော စွမ်းအင်ပမာဏ) ၉ MJME = ၉ MJME&nbsp;</p>
                <p>• စုစုပေါင်းလိုအပ်သော စွမ်းအင်ပမာဏ (၁၁၇) – ၉ = ၁၀၈ MJME (အစာကြမ်းမှ ရရှိရမည့်စွမ်းအင်ပမာဏ)</p>
                <p>• ၁၀၈/ ၈.၅ = ၁၂.၇ ကီလိုဂရမ် မွန်ဘားစားမြက် အခြောက်အလေးချိန် (အရည်သွေးပြည့် အစာကြမ်းပမာဏ)</p>
                <p>• မွန်ဘားစားမြက် အခြောက်ပမာဏ (၂၀%) ရှိသော အစာ၏ အစိုအလေးချိန်ပမာဏ - ၁၂.၇ ကီလိုဂရမ်/ ၂၀ ရာခိုင်နူန်း =
                    မွန်ဘားစားမြက် ၆၃.၅ ကီလိုဂရမ်</p>
                <p>မြန်မာ-နယူးဇီလန် ကောင်းမွန်သော နို့စားနွား စီမံကိန်း</p>
