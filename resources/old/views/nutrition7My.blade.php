                <p><strong>ခန္ဓာကိုယ် အလေးချိန် (၅၅၀ ကီလိုဂရမ်) ရှိသော နို့ပေးနွားမသည် မွေးဖွားရန် ရက်သတ္တပတ်ပေါင်း (၁၂) ပတ်
                        လိုအပ်လျက်ရှိပြီး ယခုအချိန်ထိ နွားနို့ ၆ ပိဿာ (သို့) ၉.၆ လီတာ ထွက်ရှိ သည်။ ထို နွားမကို
                        ကျွေးရန်အစာတွင် စွမ်းအင် မည်မျှ လိုအပ်မည်နည်း။&nbsp;</strong></p>
                <p>• ခန္ဓာကိုယ်ရှင်သန်ရန် အမှန်တကယ်လိုအပ်သော စွမ်းအင် ပမာဏ(Maintenance )တွက် လိုအပ်သော စွမ်းအင်ပမာဏ
                    =&nbsp;&nbsp; ၆၃ MJME</p>
                <p>• နို့ (၆) ပိသာ ထွက်ရှိရန် လိုအပ်သော စွမ်းအင်ပမာဏ= နို့ ၆ x ၉.၃ = ၅၅.၈ MJME</p>
                <p>• ကိုယ်ဝန်ဆောင်ကာလ (၁၂) ပတ်တွက် လိုအပ်သော စွမ်းအင်ပမာဏ = ၁၃ MJME</p>
                <p>စုစုပေါင်း လိုအပ်သော စွမ်းအင်ပမာဏ = ၁၃၂ MJME/ရက်</p>
                <p>၎င်းနွားမကို CP ၀၀၆ စပ်စာ&nbsp; ၄ ကီလိုဂရမ် စတင်ကျွေးမွေးနေ ပါသည်။ သို့ဖြစ်ပါ၍ လိုအပ်သော စွမ်းအင် အတွက်
                    မွန်ဘားစားမြက် မည်မျှ ကျွေးမွေးရန် လိုအပ်ပါသနည်း။ မွန်ဘားစားမြက်တွင်ပါဝင်သော အသားဓာတ်ပမာဏ (၈.၅
                    ရာခိုင်နှုန်း)၊ အစိုဓာတ် (၂၀ ရာခိုင်နှုန်း)</p>
