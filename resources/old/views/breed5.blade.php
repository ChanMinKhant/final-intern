                <p> Calving is an important time for a heifer. Heifers are strong creatures so there is no difficulty in
                    giving
                    birth to calves. However, sometimes help is needed at birth. So, how to take care of the cows while they
                    are giving birth. If necessary, we will show you how to help.&nbsp;</p>
                <p>You will see a change in the cow's behavior before the calve is born. A few days before birth, the udders
                    will be full. On the day of birth, the cow will become restless.
                    Milk will be dripping from the milk pillars. Mucus will fall due to extreme hunger. At this time, he
                    must watch whether his heifer is comfortable to give birth. But the cow must not be disturbed.&nbsp;
                </p>
                <p>You will see the heifer squeezing while giving birth. The cow must be given a clean place and created to
                    be
                    normal. If the heifer is pumping but the calf has not come out after 2 hours, she is in labor and needs
                    help. The calve is in the water bag. If the water boils, you will see fluid coming out.
                    If the calve doesn't come out after the liquid comes out, the heifer needs help.</p>
                <p>It takes some time for heifers to first calve and they need help in time. Otherwise, the calve may lose
                    its life. To help the heifer calve, you have to reach into its udder and pull it out. Care must be
                    taken. Long gloves are required. If you don't have gloves, wash your hands with soap. The rear of the
                    cow also needs to be cleaned. The hand inserted into the heifer's birth canal should be oiled to make it
                    smooth.
                    If there is a vet-grade oil readily available, you can also use herbal oil.
                    Cows have difficulty giving birth because the calf is in the wrong position.
                    Normally the calf will start out with the front pair of hands. If the hind legs start to come out / the
                    head is twisted back, then the calve should come out later.
                    The pictures below show the forms that a calve can take.
                </p>
                <div class="image-container text-center">
                    <img style="padding: 20px;"
                        src="https://greenway.sgp1.digitaloceanspaces.com/editor_images/December2020/gbWO3l4oqO7HKNgRAjDPVumFGEpuVAASSBlS3YdU.png"
                        alt="">
                </div>
                <p>Usually you will see the calve's front arms first, then the nose. To be able to breathe air from the
                    outside.</p>
                <p>When helping a cow to give birth, the first hand should be inserted into the birth canal and the calf's
                    head and front hand should be tried. If the head hangs back, it must be shaped correctly.
                    To do this, you have to push the calf back a little, create space, straighten it out, and pull it back
                    out. In the picture below, you will see a sample of the rear leg that has been reworked.</p>
                <div class="image-container text-center">
                    <img style="padding: 20px;"
                        src="https://greenway.sgp1.digitaloceanspaces.com/editor_images/December2020/4hOzTaga5h61HBUeGH3VAVCFjVOIK4M0DPhuUGb4.png"
                        alt="" width="610" height="258">
                </div>
                <p> If the calf is in the right shape, it can be easily pulled out. Can be pulled out by hand. However,
                    since it can slip, it can be pulled with a rope or chain. The cable used must be clean. To ensure that
                    the tension of the rope is applied to the entire leg when pulling, one rope should be tied above the
                    calf's shin and another below. So as not to break the leg. It can be convenient if you tie one side of
                    the leg and pull it out. However, if there are two ropes, it is more convenient to pull both legs. You
                    can see it in the picture below.</p>
                <div class="image-container text-center">
                    <img style="padding: 20px;"
                        src="https://greenway.sgp1.digitaloceanspaces.com/editor_images/December2020/WjQAEEzuoXmlshzKfkxei3IhWvKSd0AdSmjEI4dn.png"
                        alt="">
                </div>
                <p>It takes more than one person to help pull the calve out. However, in order not to harm the calve and the
                    mother, do not pull with force.</p>
                <p> If the calve is too big, Even if it is in a wrong position, it will not be able to give birth normally.
                    In such a situation, a surgical delivery must be performed.
                    It should be done by a professional veterinarian. If the calve is weaned on time and the mother heifer is
                    well taken care of, it will recover in a few weeks. However, there is a small risk of such an operation,
                    so we must facilitate the delivery as much as possible. If the calve is unable to give birth even after a
                    long period of time, the calf may die inside. The dead calve must be removed from the heifer's womb as much
                    as possible before it rots. Should be discussed with a doctor.</p>
                <p>Air through the umbilical cord while the calve is in the mother's womb. It is growing well because it is
                    getting water and nutrients. After the umbilical cord is cut when it comes out of the mother's womb,
                    there is no air supply. It is very important to be able to breathe quickly on its own in order to
                    continue living. Normally, a newborn calve has mucus in its nasal cavity. Therefore, by touching the nose
                    with something like grass and making a sneeze, the mucus in the respiratory tract will come out and you
                    will be able to breathe well. To ensure that there is no difficulty in giving birth, and if it becomes
                    difficult, you can facilitate the birth by positioning the calve and pulling it out.</p>
                <p>The main causes of calving difficulties are that the calve's body grows faster than normal due to the fact
                    that heifers are fed more food around the time of calving.
                    More than 5% of the cows on the farm experience calving difficulties because their heifers are too fat to
                    give birth. It can also be caused by other fertility problems.
                </p>
