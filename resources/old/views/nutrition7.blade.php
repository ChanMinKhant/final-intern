                <p><strong>A lactating cow with a body weight (550 kg) needs 12 weeks to give birth, and so far produces 6
                        pounds or 9.6 liters of milk. How much energy would be required in the feed to feed that
                        cow?&nbsp;</strong></p>
                <p>• The amount of energy required to actually survive the body (Maintenance)=&nbsp;&nbsp; 63 MJME</p>
                <p>• Energy needed to produce milk (6)peittha = 6 x 9.3 milk = 55.8 MJME</p>
                <p>• Energy needed for 12 weeks of pregnancy = 13 MJME</p>
                <p>• Total amount of energy required = 132 MJME/day</p>
                <p>• This cow is CP 006 text&nbsp; 4 kg started feeding. So how much Mombaca grass do you need to feed for
                    the
                    required energy? The amount of protein contained in Mombaca gress (8.5 percent), moisture (20 percent)
                </p>
                <p>• CP 006 - 4 kg x (the amount of energy contained in the food)&nbsp; 9 MJME = 36 MJME&nbsp;</p>
                <p>• Total energy required (132 ) – 36 =&nbsp; 96 MJME (amount of energy to be obtained from raw food)</p>
                <p>• 96 / 8.5&nbsp; =&nbsp; 11.3 kg (dry weight) of Mombaca grass (amount of whole liquid feed)
                </p>
                <p>• The wet weight of the food with dry amount of Mombaca grass (20%) is 11.3 kg / 20 percent = 57 kg of
                    Mombaca grass</p>
                <p><strong>Energy and feed requirements of lactating heifers</strong></p>
                <p>A lactating cow with a body weight of 550 takes 12 weeks to give birth and still produces 10 pounds or 16
                    liters of milk. How much energy will be required in the feed to feed that cow?&nbsp;</p>
                <p>• The amount of energy actually needed for the body to survive (Maintenance) =&nbsp;&nbsp; 63 MJME</p>
                <p>• Amount of energy required to produce milk (10) peittha = 10 peittha of milk &nbsp; x&nbsp; 9.3 = 93
                    MJME</p>
                <p>• The amount of energy needed to calculate the pregnancy period (12) =&nbsp; 13 MJME</p>
                <p>• Total amount of energy required = 169 MJME/day</p>
                <p>This cow was treated with 6 kg of CP 006 feed&nbsp; Start feeding. So how much Mombaca grass do you need
                    to feed for the required energy? Amount of protein (8.5 percent), moisture content (20 percent) in
                    Mombaca grass
                <p>CP 006- 6 kg x (amount of energy contained in dry food) 9 MJME = 54 MJME&nbsp;</p>
                <p>Total required amount of energy (169) – 54=115 MJME (amount of energy to be obtained from raw food)</p>
                <p>• 115/ 8.5 = 13.5 kg of Mombaca grass dry amount (full liquid raw food amount)</p>
                <p>• Wet weight of feed with dry amount of Mombaca grass (20%) - 13.5 kg/20 percent = 67.5 kg of Mombaca
                    grass</p>
                <p><strong>Energy and feed requirements of lactating heifers</strong></p>
                <p>How much energy will a dairy cow with a body weight (550) need in the feed to feed a dry heifer that
                    needs two (2) days to give birth?</p>
                <p>• Amount of energy actually needed to survive the body (Maintenance) = 63 MJME</p>
                <p>• The amount of energy needed to calculate the pregnancy period (2) weeks = 54 MJME</p>
                <p>Total amount of energy required = 117 MJME/day</p>
                <p>This cow is being fed 1 kg of CP 006 feed. So how much Mombaca grass do you need to feed for the required
                    energy? The amount of protein contained in Mombaca grass (8.5 percent); Moisture (20 percent)</p>
                <p>• CP 006 - 1 kg x (amount of energy in raw food) 9 MJME = 9 MJME&nbsp;</p>
                <p>• Total required amount of energy (117) – 9 = 108 MJME (amount of energy to be obtained from raw food)
                </p>
                <p>• 108/ 8.5 = 12.7 kg dry weight of Mombaca grass (amount of liquid raw food)</p>
                <p>• Wet weight of feed with dry amount of Mombaca grass (20%) - 12.7 kg / 20 percent = 63.5 kg of Mombaca
                    grass</p>
                <p><strong>***Since the animal cannot use all the energy contained in the food, they use METABOLISABLE
                        ENERGY or Megajoules of Metabolisable Energy (MJME) to measure the amount actually used by the
                        animal. ***</strong></p>
