                <p>July August and September are the months when most dairy farmers in Myanmar have access to plenty of good
                    quality raw feed.</p>
                <p>It is important to know the amount of green fodder one has planted and the approximate level of
                    sufficiency for one's cattle during the period of abundant green fodder.</p>
                <p>During the rainy season, there is not only enough raw food, but some farmers may have an excess.</p>
                <p>If there is a surplus, how will it be kept for the cattle? The best way to preserve excess raw food with
                    the least loss of nutritional value and for a long time is by making cattle's silage.</p>
                </span>
                <p>First time - 18 tons = 18000 kg = 11250 peittha</p>
                <p>Second time - 20 tons = 20,000 kg = 12,500 peittha</p>
                <p>Total - 38 tons = 38,000 kg = 23,750 peittha</p>
                <p>Weighing with an 8-peittha basket, we can get 2968 baskets.</p>
                <p>In order to get enough production for 1 dairy cow, if you are feeding soft feed (total of 3 peittha of
                    cottonseed meal and soybean meal), you need to feed 5 baskets per day with 8 peittha of wet millet.</p>
                <p><span style="color: #008000;"><strong>1 dairy cow -&nbsp;</strong></span></p>
                <p>Raw food requirement for 1 day meal = 5 baskets</p>
                <p>Raw food requirement for 1 salary = 150 baskets</p>
                <p>Rainy season (from July to October) raw food requirement = 600 baskets</p>
                <p>Number of baskets available from 1 acre = 2968</p>
                <p>Number of raw feed required for 1 dairy cow for 4 months of rainy season = 600 baskets</p>
                <p>From 1 acre, enough dairy cows for 4 months of rainy season = 5 cows</p>
                <p>A farm with 1 acre of corn planted and 5 dairy cows is enough to feed only 4 months of the rainy season.
                </p>
                <p>A farm that grows 1 acre of corn and raises 3 dairy cows is enough to feed for 3 months of rainy season +
                    2 and a half months of summer.</p>
                <p>Raiders are encouraged to increase the production of their cows during the rainy season and to reduce the
                    cost of feed in the rainy season.They are fed with fermented cow feed.&nbsp;</p>
                <p>&nbsp;</p>
                <p><strong>Source - MDEP</strong></p>
