            <div class="page-content">
                <p>
                    Achieving good reproductive performance of cows is one of the important factors for the
                    profitability of livestock farming.
                </p>
                <p>
                    If the heifers have high calving ability and calve regularly once a year, the farm will have more
                    calves.
                </p>
                <p>
                    Cows have their peak milking period in the first few months after giving birth to a calf. After
                    that, the milk yield gradually decreases every month.
                </p>
                <p>
                    In order for cows to recover their udders and produce good milk after giving birth to another calf,
                    they should be given a rest period (2 months) before giving birth to that calf.
                </p>
                <p>
                    Most of the breeders tend to express milk for 12-15 months before weaning. If you continue to
                    express milk for several months without such a milking period, you will experience a decrease in
                    milk production in the next calf. In addition, the total amount of milk available over its lifetime
                    will also decrease.
                </p>
                <p>
                    If the heifers are giving birth to calves on a regular basis, the number of cows on the farm may
                    increase rapidly.
                </p>
                <p>
                    For example, if a 12-year-old heifer on a farm gives birth to her first calf at 2 years of age and
                    has one calving year, that cow may produce 10 calves by 12 years of age.
                </p>
                <p>
                <p>However, if that cow does not give birth for a year and then gives birth to another child after 1
                    and a half years (18 months), she will only be able to give birth to a total of 7 calves at the age
                    of 12. The benefit available in these two situations is recalculated in detail in the figure below.
                </p>
