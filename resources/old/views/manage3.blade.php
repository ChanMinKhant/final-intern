                <p>
                    According to this example data, if a heifer has good reproductive performance, her lifetime total
                    milk income will be 230 lakhs. However, poor reproductive performance due to poor farm management
                    would result in a total milk income of only 153 lakhs over its lifetime.
                </p>
                <p>
                    Therefore, it is important for the breeders to feed the cows on their farms with sufficient
                    nutrition and manage them well to ensure good fertilization rates in order to benefit from the farm.
