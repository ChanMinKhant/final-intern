                <p class="text-center"><b>Mulato</b></p>
                <p>High production and nutrition. It looks like signal grass. Ability to tolerate dryness.
                    Less productive under shady plants. Can be planted mixed with legumes. (20-30 days) can be harvested,
                    transported and fed. It is difficult to produce seeds.</p> <br>
                <br>
                <p class="text-center"><img class="text-center"
                        src="https://greenwaymyanmar.com/uploads/image/October2018/4ZfCzi0dPbBGik24JlsjNbEBLZknKKtpRBlO0110.png"
                        alt=""><br><b>Centuriion centro </b></p>
                <p>It is a type of bean that is planted once a year.
                    Can be watered seasonally. High in nutrients.
                    It should be dried and stored. Good seed yield.</p> <br><br>
                <p class="text-center"><img
                        src="https://greenwaymyanmar.com/uploads/image/October2018/4OaM7bTmcdbsZUBTvho2T3Lz18T0xzjSH6odKZB0.png"
                        alt=""><br><b>Monbaca Guinea</b><br>Can thrive in shady areas.
                    It should be planted in well-fertilized, moist, well-drained areas. When transplanting from a nursery,
                    grasses can be planted 16 inches apart. Can be mixed with other types of grass and beans. (3-4 weeks
                    apart) can be harvested and fed.
                    It is difficult to produce seeds. Rich in nutrients.</p> <br><br>
                <p class="text-center"><img
                        src="https://greenwaymyanmar.com/uploads/image/October2018/mYwdkLfd6bzId53Ps059PsPmtIyPQdRUiEI8RTW7.png"
                        alt=""><br><b>Gatton panic</b> <br>It can withstand drying more than guinea grass. The leaf
                    frog can control the moisture. It can grow in moderately fertile soils. Can be planted in the shade.
                    Can be planted under mango plantations. Can be planted mixed with pasture beans and grasses. After
                    planting (3-4) weeks, harvesting, transportation, free schooling, etc. can be fed. </p> <br> <br>
                <p class="text-center"><img style="width: 340px; height:220px;"
                        src="https://davisla3.files.wordpress.com/2012/08/panicum-coloratum.jpg"
                        alt=""><br><b>Bambatsi Panic</b> <br>Can be planted in any type of soil.
                    It can also survive in sandy soils with cracked clays. Susceptible to floods and seasonal inundation.
                    Ability to withstand drought. Good taste and rich in nutrients.</p> <br> <br>
                <p class="text-center"><img
                        src="https://greenwaymyanmar.com/uploads/image/October2018/LuNIVvdS6sGCq58YHYEcHLt1sJZvNDMnmHwkjbv7.png"
                        alt=""><br><b>Buffel grass</b><br>It should be planted in dry areas and yields well.
                    After planting once, the seeds can survive automatically. Can be planted in any soil.
                    Can be sent to school/cut feed.</p><br><br>
                <p class="text-center"><img
                        src="https://greenwaymyanmar.com/uploads/image/October2018/YXk14s9QtY1S3yvcEn2SkBYfwIgWaye0swqydxsY.png"
                        alt=""><br><b>Desmanthus</b><br>3-4 feet tall. Can be harvested, transported and fed.
                    Not suitable for school. It has high nutrient content and can be grown everywhere. It is more developed
                    in the acid and salinity of the soil (7). It can survive under moderate shade, but the development is
                    weak if it falls in the shade. Can be planted as seed or seedling.
                    They should be planted 3-4 feet apart from each other. Can be harvested and fed once in (3-4) weeks. It
                    can produce 130-200 kg of seeds per acre.</p> <br><br>
                <p class="text-center"><img
                        src="https://greenwaymyanmar.com/uploads/image/October2018/6GORcoN412qfkOoPKMCNI0o3rQRGQrwpATD2Pl9F.png"
                        alt=""><br><b>Shrubby stylo</b><br>Can be planted with other grasses. It should be planted
                    before the rainy season and transplanted during the rainy season. Early in the season, seeding is
                    difficult and yield is low. Both have slow but good seed germination and production.
                    Although it is low in nutrients, it can be grown as a pasture for dairy cows.</p> <br><br>
                <p class="text-center"><img
                        src="https://greenwaymyanmar.com/uploads/image/October2018/1dfnkgQgjDx63cY7HR4L9c7XgWYwnzUXdQZFL2d3.png"
                        alt=""><br><b>Splenda Setaria</b><br>Can be planted on any type of soil. Fertile soil
                    The land has high production. It is not suitable for planting in wet and shady areas.
                    It is drought tolerant, but yields decrease during drought. Easy to harvest.
                    Can be mixed with other types of grass and beans.</p><br><br>
                <p class="text-center"><img
                        src="https://greenwaymyanmar.com/uploads/image/October2018/whHCCOlLFAAjAlatUuk6UA5kfoL2y7S0dXM9yYOL.png"><br>Arachispintoi<br>မြန်မာနိုင်ငံတွင်
                    It is often planted as an ornamental pea flower. In other countries, it is grown as pasture in areas
                    with good rainfall. Can be mixed with grasses. Can be planted on any type of soil.
                    It has the ability to provide good fertility by planting in low-fertilized soils. Can be planted by
                    cutting the root joint (3-4). Leaves must be removed before planting. It is drought tolerant but reduces
                    production. Can withstand flooding. High nutritional content
                    Improves milk production. Flatulence cannot occur in cows.</p>
