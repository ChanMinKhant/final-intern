                <p>Asexual inbreeding means that a breeding specialist injects the bull's semen into the heifer's
                    reproductive tract to produce an embryo.</p>
                <p><strong><span style="color: #993300;">Benefits of Asexual Inbreeding</span></strong></p>
                <ul>
                    <li>No need to buy bulls and raise them on the farm at all costs</li>
                    <li>Able to prevent and control from sexually transmitted diseases due to the use of a single cow.</li>
                    <li>Ability to import and distribute new breeds of purebred cattle</li>
                    <li>Ability to raise the breed of cattle on the farm</li>
                    <li>Able to transport the semen of high-breed bulls everywhere</li>
                    <li>A single high-breed bull can inseminate many heifers with a single semen</li>
                    <li>Being able to prevent the cow from being injured due to mating with the bull</li>
                </ul>
                <p>A castration specialist inserts a specially designed syringe into the uterus of a calving heifer as
                    shown.
                    Then, to fertilize, the liquid from the liquid is placed in the place closest to the egg. In doing so,
                    one hand of the midwife is inserted into the anus, testing the uterine canal below and guiding the
                    syringe. A well-trained inbreeding specialist can achieve calving rates similar to inbreeding with
                    natural bulls without causing pain to the heifer.
                </p>
                <p> Sterilized milk is a liquid which is extracted from a cow, put into a small tube, frozen and stored. If
                    the embryo has not been sterilized, it must be stored in liquid nitrogen in a special storage container.
                    If there is a shortage of nitrogen in the storage container, it can affect the germination rate of the
                    seedlings.</p>
                <p> Most breeders think that when breeding bulls are available for free (or cheap) the cost of a crossbreed
                    is high. However, consideration should be given to the cost of raising bulls on the farm (or the
                    possibility of cross-breeding with outside bulls). If it is bred with a sire of good pedigree, the cost
                    can be recouped when the daughter of that bull is milked.</p>
                <p> For example, if a calf born to a crossbred sire produces more milk at lactation, the cost of inbreeding
                    will be recouped at a later time. If the cost of a male hybrid is 20,000 kyats, if the mare produced by
                    the hybrid produces one (1) pound of milk, the milk must be sold for (17) days and the cost must be
                    returned. Over the lifetime of that cow, the benefit of increased milk production will be about 26 lakhs
                    more. That profit depends on the quality of the bull produced. If that's the case, then the cost of the
                    twins is the right investment.</p>
                <p> There are many reasons why a breeder may breed a heifer many times without success. Some of the main
                    points are:</p>
                <ul>
                    <li>Poor handling and maintenance (e.g., spermatozoa die if not properly cooled)</li>
                    <li>When it's time to get pregnant</li>
                    <li>Weakness of asexual hybrid skills</li>
                    <li>Lack of good health of the heifer at calving (or near calving)</li>
                </ul>
                <p> If you experience poor breeding success with the same breeding method, you should talk to other breeders
                    who are using that breeder.
                    If other breeders don't have a problem, it's not because of the breeding specialist, but because of the
                    cow's defect. It may be due to the wrong timing of sperm and mating. If other breeders have the same
                    problem, it may be a defect in the breeder (or salivary gland).
                    If many factors are right, a crossbreeding with a non-sex breed can have the same fertility rate as a
                    crossbreed with a purebred bull. If you can use a good sucker rod produced by a quality breed bull, you
                    are encouraged to perform asexual crossbreeding. If inbreeding is not possible, you should consider
                    buying a good bull and sharing it with some nearby breeders.</p>
                <p><span style="color: #993300;"><strong>Precautions to be taken during pregnancy</strong></span>
                </p>
                <ul>
                    <li>Calf/crossbred cattle must be as described without being too thin or too fat.
                        <div class="image-container text-center">
                            <img style="padding: 20px;"
                                src="https://greenway.sgp1.digitaloceanspaces.com/editor_images/December2020/ROwxtK7mXNKJy99hrUjuAmVrnNPtcAtS60XmgSdO.png"
                                alt="">
                        </div>
                    </li>
                    <li>If too thin lactating heifers can be weaned early and fed to reach the standard body position at the
                        time of calving, the milk yield will be good during lactation.</li>
                    <li>3 months after calving is the time when the milk production is at its highest and the body is too
                        thin, so the heifers need to have the body as described before calving.</li>
                </ul>
