                <p> Milk fever is a physiological disorder caused by calcium deficiency.
                    Milk fever can also be associated with other diseases. </p>
                <p> <span style="color: #008080;"><b>Reasons</b></span></p>
                <p> During post-pregnancy, childbirth, milk production, milk production, and early lactation, the need for
                    calcium increases. However, at that time, the cow's ability to transform and utilize calcium from her
                    bones and absorb calcium from food is also decreasing. Some cows can't replace the calcium they need
                    enough and develop milk fever. It is more common especially in high producing cows.</p>
                <p> Milk fever is common around birth and around 3 days after birth. However, depending on the milk yield of
                    the cow, it can happen at other times depending on the feeding system and supplementary feeding. </p>
                <p> It is more common in woolly cows than in heifers. Cows produce more milk, due to their age, the
                    inability to quickly replace calcium in the blood, etc. </p>
                <p> <span style="color: #008080;"><b>Symptoms</b></span></p>
                <p> <span style="color: #008080;"><b>Step (1)</b></span></p>
                <p> - Decreased feeding rate</p>
                <p> - restlessness; Quick response</p>
                <p> - weakening</p>
                <p> - Bending of hind legs when walking</p>
                <p> <span style="color: #008080;"><b>Step (2)</b></span></p>
                <p> - Head tilting or turning the head to the side of the body (S-shaped rotation)</p>
                <p> - Ear tips are cold</p>
                <p> - Dryness of the tips of the nose</p>
                <p> - Imbalance in walking</p>
                <p> - Muscle tremors</p>
                <p> - Constipation; urinary retention Lower than normal temperature</p>
                <p> <span style="color: #008080;"><b>Step (3)</b></span></p>
                <p> - Lying down without being able to get up. Feeling weak</p>
                <p>- Rapid heartbeat; Increased heart rate</p>
                <p> <span style="color: #008080;"><b>Healing</b></span></p>
                <p>A cow with milk fever can die if left untreated, so treatment must be given as soon as possible.
                    Calcium (calcium gluconate, calcium borogluconate...) should be provided.
                <p> Calcium <br> depending on the severity of the disease
                    - Oral treatment
                    - Subcutaneous administration
                    - It can be given by intravenous injection.
                </p>
                <p>Intravenous administration should be treated by a veterinarian as it is associated with pulse/heart rate.
                    Magnesium is also important in calcium production. </p>
                <p> <span style="color: #008080;"><b>Protection</b></span></p>
                <p>The cow's body condition must be neither too fat nor too thin.
                    A fat cow is more prone to milk fever. </p>
                <p>Because milk is produced during the lactation period, Di-calcium phosphate (DCP) should be added to the
                    feed to cover the calcium deficiency from the diet, but DCP should not be fed during the lactation
                    period. Because milk is not produced during the lactation period, the calcium requirement is not as high
                    as during the lactation period. </p>
                <p>During the lactation period, when the calcium obtained by feeding DCP is more than the requirement, the
                    absorption of calcium from the intestine and the stimulation of the calcium production process in the
                    body are weakened. The sudden transition from the weaning period to the post-weaning period causes
                    damage.</p>
                <p>If you want to increase the body condition and milk yield of the cow, you must feed it based on energy so
                    that the cow does not become overweight.</p>
                <p>By giving calcium feed supplements (DCP) immediately after calving and later
                    It helps. (Do not give early before those times. Do not give during lactation.
                    Manage feeding well.)</p>
