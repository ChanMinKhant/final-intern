                <p>Deciding when to sell heifers is not easy for breeders.
                    One breeder and another have different opinions about selling cows.
                    The information I want to give you now is what you should think about in order to run a profitable
                    farm.&nbsp;</p>
                <p>How do you know when to sell a heifer? <br> - We have to decide based on how the cow can benefit us going
                    forward.
                    We can't always know this for sure, but we can make good guesses.&nbsp;</p>
                <p><span style="color: #008080;"><strong>You should consider the following points.</strong></span></p>
                <ul>
                    <li>Would you like to increase the number of cows on the farm or keep the current number as it is?</li>
                    <li>Quality and quantity of heifers on the farm</li>
                    <li>milk yield of cows, age Health Reproductive background</li>
                    <li>Market conditions for heifers and mature cows</li>
                </ul>
                <p>
                    Usually, the number of cows that can be on the farm depends on the ability to provide food. There are
                    limitations due to milk market or farm size etc. If you want to increase the number of cows on the farm,
                    you must decide carefully what kind of cows to keep. Cows that will lose money. Heifers with
                    reproductive problems must be sold. If you want to maintain the number of cows on the farm, you need to
                    think more.
                </p>
                <p>
                    We always recommend proper breeding of heifers to breed cows with good bulls.Each year, the
                    first cubs must be of better quality than their mothers. The milk yield of heifers is usually low in the
                    first year of lactation, but will increase in the second year. In order to maintain high genetic
                    quality, the good heifers must be kept and the bad heifers sold. Depending on the quality of the heifers
                    and heifers, approximately 20% of the heifer group should be replaced with heifers each year. If the
                    farm has many good quality heifers, older heifers, Low-producing cows must be sold.
                </p>
                <p>
                    Cows that are not fertile should be sold as they have a long lactation period and spend money on it. A
                    cow with good production, but not pregnant. If there are many months without milk, it should be sold. By
                    keeping records, you can see which cows have such problems.
                </p>
                <p>
                    Another thing is milk production. Most breeders have already thought this through. Also, the age of the
                    cows must be considered. Even if a heifer is milking well, it is better to keep a younger cow than an
                    older one. Older cows are more prone to health and reproductive problems and you never know when they
                    will develop. It is safer to keep a 3-year-old cow that gives 6-peittha a day than a 10-year-old cow
                    that
                    gives 8-peittha a day.
                </p>
                <p>
                    The health of the cow must be considered. Is there a breed of cow that has something wrong with it that
                    is causing problems? For example,frequent leg pain or mastitis.
                </p>
                <p>
                    Finally, you must consider the market for the heifers and heifers that will be culled. These prices are
                    subject to change. If the market for cull cows is not good, it may be better to keep those cows and have
                    more milk cows for the year than to sell them. Or, if the market for heifers is good, it may be better
                    to select and sell the surplus heifers and milk fever-prone cows.
                </p>
                <p><strong>Source - MDEP</strong></p>
