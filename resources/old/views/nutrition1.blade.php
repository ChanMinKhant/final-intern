    <h3 class="page-title mt-3 " style="padding-top: 25px; margin-bottom: 40px;">
        <strong>What is an important transition period in dairy cattle?</strong>
    </h3>

    <div class="page-cover text-center" style="max-height: 400px; overflow: hidden; padding-bottom:30px;">
        <img src="https://greenway.sgp1.digitaloceanspaces.com/post_covers/72c33e91a72aea091becd39bd4b02235.jpg"
            class="page-cover img-fluid" style="max-height: 400px;">
    </div>
    <br>
    <div class="container">

        <div class="page-body">
            <div class="page-content">
                <p>- The period of transition from pregnant and lactating to calf-bearing and lactating. The period between
                    (3) weeks before birth and (3) weeks after birth is defined as the transition period.</p>
                <p>- This is the time when the cow is adjusting to the changes in nutrition and hormone production.</p>
                <p>- The most important physiological changes during this period are the reduction of dry matter intake
                    around the time of calving, the need for more nutrients to produce milk and milk.</p>
                <p><span style="color: #008080;"><strong> *** Why is the transition period important?</strong></span></p>
                <p>- Cows that make a good transition from lactation to lactation will have good health, production and
                    reproduction.
                </p>
                <p>- <span lang="hi-IN">If it cannot be converted conveniently, it will cause a decrease in milk yield,
                        decrease in immune system, decrease in reproductive functions, etc., and cause economic
                        losses.</span></p>
                <p>- <span lang="hi-IN">During the last week of pregnancy, the fetus and placenta are in a state of
                        greatest need for energy, as is the calf after birth.
                        It's a time when a lot of energy is needed to make milk and promote milk production.</span></p>
                <p>- <span lang="hi-IN">However, during the transitional period such as pregnancy, childbirth, and
                        breastfeeding, hormonal changes, physical and mental changes, and stress causes the rate of eating
                        to decrease and the availability of energy from food also decreases.</span></p>
                <p>*** <span lang="hi-IN">The state in which energy supply is less than energy demand in cows is called
                        negative energy balance.</span></p>
                <p>- <span lang="hi-IN">When the energy is insufficient, the body fat stored in the cow's body is broken
                        down and used to obtain the necessary energy. As a result, body weight and body condition decrease.
                        In addition, when the production is more than the consumption of fats into energy, the side product
                        Beta-Hydroxy Butyric Acid (ketone body) increases in the blood and causes ketosis.</span>
                </p>
                <p>- <span lang="hi-IN">Other related diseases may also occur due to insufficient energy.
                        In addition, hormonal imbalance and immune system reduction occur.</span></p>
                <p>- <span lang="hi-IN">2 weeks before birth and 2 weeks after birth is a period when the immune system is
                        more vulnerable. At that time, mastitis, metritis and retained placenta may occur. It also makes it
                        possible for other infectious diseases to happen.</span></p>
                <p>- <span lang="hi-IN">Due to insufficient nutrients and hormonal imbalance, late calving, late
                        ovulation, low fertilization rate, etc., the problem is that the interval between one child and
                        another is long and the child cannot be born for a year.</span></p>
                <p><span style="color: #008080;"><strong> ***Problems related to health deterioration***</strong></span></p>
                <p>(<span lang="hi-IN">1</span>)Energy-Related Disorders</p>
                <p> <span lang="hi-IN">
                        - Rumen acidosis/ Lameness<br>
                        - Ketosis<br>
                        - Fatty liver syndrome<br>
                        - Displaced abomasum </span></p>
                <p>(<span lang="hi-IN">2</span>) Mineral-Vitamin Related Disorders</p>
                <p>- Milk fever<br> - Udder edema</p>
                <p>(<span lang="hi-IN">3</span>) <span lang="hi-IN">Immune System Related Disorders</p>
                <p><span lang="hi-IN">
                        - Retained placenta<br>
                        - Metritis<br>
                        - Mastitis
                    </span></p>
                <p>*** <span lang="hi-IN">Good nutrition and feeding management during the transition period can help
                        increase dry matter intake after calving, facilitate calving, improve cow health, milk production
                        and reproductive performance.</span></p>
                <p>&nbsp;</p>
                <p><strong>Source - MDEP</strong></p>
