                <p> &nbsp; &nbsp; နွားကလေးကို ကြိုးနဲ့ချည်ထားခြင်းထက် နွားငယ်ခြံထဲမှာထားခြင်းက ပိုကောင်းပါသည်။
                    ဒါမှသာ နွားကလေးများသည် ပိုပြီးလွတ်လပ်ပျော်ရွှင်စွာ သွားလာနိုင်၊ အစာနှင့်ရေကို လွတ်လပ်စွာ
                    စားနိုင်မည်ဖြစ်သည်။
                    တစ်ကောင်ချင်းစီကျွေးစရာမလိုတော့ဘဲ စီမံရတာလည်း ပိုလွယ်ကူမည်။</p>
                <p> &nbsp; &nbsp; နွားကလေးကို မွေးပြီးပထမအပတ်အတွင်း မိခင်နွားမကြီးနှင့်ခွဲပြီး သန့်ရှင်းသည့်နွားကလေးခြံတွင် ထားရမည်။
                    ထိုခြံသည် သန့်ရှင်းပြီး သက်သောင့်သက်သာဖြစ်စေမည့် အပူချိန်ရှိရမည်။
                    အစာနှင့်ရေ လွတ်လပ်စွာ စားနိုင်စေရမည်။ အကြီးအငယ်ကွာခြားမှုမရှိစေရန် တစ်ခြံထဲတွင် အရွယ်တူများသာထားရမည်။
                </p>
                <p> &nbsp; &nbsp; အချို့ခြံများတွင် နွားကလေးတစ်ကောင်ချင်းစီထည့်သည့် နွားငယ်လှောင်အိမ်များရှိကြသည်။
                    ထိုကဲ့သို့ တစ်ကောင်ချင်းစီခွဲထားခြင်းသည် နွားငယ်များကြားတွင် ရောဂါကူးစက်နိုင်သည့်အခွင့်အရေးကို
                    လျှော့ချနိုင်သည်။
                    တစ်ကောင်ချင်းစီ ခွဲထားခြင်းမျိုးမဟုတ်ဘဲ နွားကလေးများကို အရွယ်တူအုပ်စုလိုက်အကန့်ခွဲထားနိုင်လျှင်လည်း
                    ကောင်းပါသည်။</p>
                <p> &nbsp; &nbsp; သန့်ရှင်းသောနွားငယ်ခြံရှိခြင်းနှင့် နေမကောင်းဖြစ်သောနွားငယ်ကို အုပ်စုမှ မြန်မြန်
                    သီးသန့်ခွဲထားနိုင်ခြင်းသည် ရောဂါကူးစက်မှုကိုလျှော့ချရာတွင် ထိရောက်မှုရှိသည်။
                    နွားငယ်ခြံတစ်ခြံထဲတွင် နွားအကောင်ရေအနည်းငယ်သာရှိလျှင် ပြဿနာမရှိပါ။
                    အကယ်၍ ခြံရှိနွားကလေးများ နေမကောင်း/ရောဂါဖြစ်၍ သေနှုန်းသည် ၅ရာခိုင်နှုန်းထက်များနေပါက တစ်ကောင်ချင်းစီကို
                    လှောင်အိမ်ဖြင့် ထားသင့်သည်။</p>
                <p> &nbsp; &nbsp; နွားငယ်ခြံကို သန့်ရှင်းရလွယ်ကူပြီး သက်သောင့်သက်သာဖြစ်စေမည့် ပစ္စည်းများဖြင့် ဆောက်သင့်သည်။
                    ကွန်ကရစ်ကြမ်းပြင်သည် အဆင်ပြေသည်။ သို့သော် နွားငယ်ကလေးများအတွက် အိပ်ရတာ သက်သောင့်သက်သာရှိစေဖို့ စပါးခွံ
                    သို့မဟုတ် ကောက်ရိုးခင်းပေးပြီး ပုံမှန်လဲပေးဖို့လိုအပ်သည်။ နွားငယ်ခြံကို သစ်သားပြားအခင်းဖြင့်
                    အမြင့်တွင်ဆောက်လျှင် အညစ်အကြေးနှင့် ဆီးများ အောက်သို့ကျသွားသည့်အတွက် အခင်းသည်
                    ခြောက်ပြီးသန့်ရှင်းနေမည်ဖြစ်သည်။</p>
                <p> &nbsp; &nbsp; နွားကလေးများအတွက် သက်သောင့်သက်သာဖြစ်သော အပူချိန်သည် လူများထက် ပိုလျော့နည်းသည်။ လူတွေအတွက် ပူအိုက်သည်ဟု
                    အနည်းငယ်ခံစားရချိန်တွင် နွားကလေးများအတွက်မူ လူတွေထက် ပိုပူသည့် (heat stress) အခြေအနေဖြစ်သည်။
                    အေးသည့်အချိန်တွင်လည်း လူတွေအေးသည်ဟု ခံစားမိချိန်သည် နွားများအတွက် သက်သောင့်သက်သာအခြေအနေဖြစ်သည်။
                    ရှမ်းပြည်နယ်လို အအေးများတဲ့နေရာတွင် ဆောင်းရာသီမှာတော့ နွားကလေးများနွေးစေဖို့ ကောက်ရိုးခင်းပေးတာက
                    ၎င်းတို့အတွက်အဆင်ပြေစေမည်။ ပူသည့် အခြားသောဒေသများအဖို့ လေဝင်လေထွက်ကောင်း၊ အရိပ်ရပြီး အေးသည့်နေရာတွင်
                    နွားကလေးခြံ ဆောက်သင့်သည်။</p>
                <p> &nbsp; &nbsp; ခြံထဲတွင် ရေနှင့် အစာခွက်ထားပေးရမည်။ သန့်ရှင်းရလွယ်ပြီး အစာနှင့်ရေ ထည့်ရလွယ်ကူရမည်။
                    အလိုအလျောက်အဖွင့်အပိတ်လုပ်နိုင်သည့် ရေခွက်မျိုးထားပေးခြင်းဖြင့် နွားကလေးများ ၂၄နာရီရေရနေစေရန်
                    လွယ်ကူစေမည်။ ခြံအပေါက်ဝသည် နွားကလေးကြီးလာပြီး နို့ဖြတ်ဖို့ အပြင်ထုတ်ချိန်တွင် အဆင်ပြေစေရန် လုံလောက်သည့်
                    အကျယ်ရှိရမည်။ နို့ဖြတ်ပြီးနောက် နွားကလေးများကိုလည်း အရွယ်တူအုပ်စုလိုက် အကန့်ခွဲထားပြီး
                    ကျွေးမွေးစီမံနိုင်လျှင် ပိုကောင်းပါသည်။</p>
                <p> &nbsp; &nbsp; ဝါးနှင့် သက်ကယ်ကိုအသုံးပြုပြီး အရိုးရှင်းဆုံးနှင့် စျေးသက်သာသော နွားငယ်ခြံလည်းပြုလုပ်နိုင်ပါသည်။
                    နွားကလေးများကို သန့်ရှင်း၊အေးမြစွာထားနိုင်ဖို့၊အစာနှင့်ရေ လွတ်လပ်စွာစားစေနိုင်ဖို့သည်သာ ပို၍အဓိကကျသည်။
                </p>

