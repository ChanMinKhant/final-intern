                <div class="page-content">
                    <p>&nbsp; &nbsp; နွားမများ၏ မျိုးပွားမှုဆိုင်ရာ လုပ်ဆောင်ချက်ကောင်းများရရှိခြင်းသည် မွေးမြူရေးလုပ်ငန်း၏
                        အကျိုးအမြတ်ရရှိရေးအတွက် အရေးကြီးသော အချက်များတွင် တစ်ခုအပါဝင်ဖြစ်သည်။</p>
                    <p>&nbsp; &nbsp; # နွားမများ သားအောင်နိုင်စွမ်းမြင့်မား၍ ပုံမှန် တစ်နှစ်တစ်ကြိမ်သားမွေးနေလျှင်
                        ခြံတွင်နွားကလေးများ ပိုပြီးရရှိနေမည်ဖြစ်သည်။</p>
                    <p>&nbsp; &nbsp; နွားမများသည် နွားကလေးမွေးပြီး ပထမလအနည်းငယ်တွင် နို့ပေးကာလ အမြင့်ဆုံးဖြစ်သည်။
                        နောက်ပိုင်းတွင် နို့ထွက်နှုန်းသည် လတိုင်း တဖြည်းဖြည်း လျော့ကျသွားသည်။</p>
                    <p>&nbsp; &nbsp; နွားမများ နို့အုံပြန်လည် ကောင်းမွန်လာစေရန်နှင့် နောက်တစ်သားမွေးပြီးနောက်
                        နို့ထွက်နှုန်းကောင်းစေရန် ထိုတစ်သားမမွေးမီ နို့နားကာလအဖြစ်(၂လ) နား ပေးရမည်။</p>
                    <p>&nbsp; &nbsp; မွေးမြူသူအများစုသည် နို့နားမပေးခင်အထိ (၁၂-၁၅) လ ကြာ နို့ညှစ်ယူတတ်ကြသည်။ ထိုကဲ့သို့
                        နို့နားကာလမထားဘဲ လများစွာ နို့ညှစ်ယူနေပါက လာမည့်တစ်သားတွင် နို့ထွက်နှုန်းကျဆင်းခြင်း ကြုံတွေ့ရမည်။
                        ထို့အပြင် ၎င်း၏သက်တမ်းတစ်လျောက် ရရှိနိုင်သောစုစုပေါင်း နို့ပိဿာချိန်ပမာဏသည်လည်း လျော့နည်းပေလိမ့်မည်။
                    </p>
                    <p>&nbsp; &nbsp; အကယ်၍ နွားမများသည် နွားကလေးများ ပုံမှန်တစ်နှစ်တစ်သား မွေးဖွားပေးနေမည်ဆိုလျှင်
                        ခြံရှိနွားကောင်ရေသည်လည်း လျှင်မြန်စွာ တိုးလာပေမည်။</p>
                    <p>&nbsp; &nbsp; ဥပမာဆိုရသော် ခြံမှ အသက် ၁၂ နှစ်ရှိ နွားမတစ်ကောင်သည် အသက် ၂ နှစ်သားတွင်
                        ပထမသားမွေးပေးပြီး တစ်နှစ်တစ်သားကျပါက ထိုနွားမသည် အသက် ၁၂ နှစ်တွင် နွားကလေး ၁၀ ကောင်
                        မွေးပေးနိုင်ပေမည်။&nbsp;</p>
                    <p>&nbsp; &nbsp; သို့သော် ထိုနွားမသည် တစ်နှစ်တစ်သားမကျဘဲ ၁ နှစ်ခွဲ (၁၈လ) ကြာမှ နောက်တစ်သားကျနိုင်ပါက
                        အသက် ၁၂ နှစ်တွင် စုစုပေါင်း နွားကလေး ၇ ကောင်သာ မွေးပေးနိုင်မည် ဖြစ်သည်။ ၎င်းအခြေအနေနှစ်ခုတွင်
                        ရရှိနိုင်သော အကျိုးအမြတ်ကိုအောက်ပါပုံတွင် အသေးစိတ်ပြန်လည်တွက်ချက်ပြထားပါသည်။</p>
