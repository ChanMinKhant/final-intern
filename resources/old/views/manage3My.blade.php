                    <p>&nbsp; &nbsp; ဤဥပမာ အချက်အလက်များအရ နွားမတစ်ကောင်သည် မျိုးပွားမှုဆိုင်ရာ လုပ်ဆောင်ချက်ကောင်းမွန်ပါက
                        ၎င်း၏သက်တမ်းတစ်လျှောက်ရရှိနိုင်သော နွားနို့ဝင်ငွေ စုစုပေါင်းမှာ သိန်း ၂၃၀ ရရှိမည်ဖြစ်သည်။ သို့သော်
                        ခြံစီမံခန့်ခွဲမှု မကောင်းသောကြောင့် မျိုးပွားမှုဆိုင်ရာ လုပ်ဆောင်ချက်မကောင်းခဲ့သော်
                        ၎င်း၏သက်တမ်းတစ်လျှောက် ရရှိနိုင်သော နွားနို့ဝင်ငွေစုစုပေါင်းမှာ ၁၅၃ သိန်းသာ ရရှိမည်ဖြစ်သည်။</p>
