                <p>&nbsp; &nbsp; နွားမကြီးနွားကလေးမွေးချိန်တွင် ကောင်းစွာစီမံခြင်းဖြင့် မွေးဖွားရလွယ်ကူစေခြင်း၊
                    မွေးပြီးနောက် ကျန်းမာစွာရှိခြင်း၊ နို့ပေးကာလအစောပိုင်းတွင် နို့ထွက်နှုန်းများခြင်း စသည်တို့
                    ရရှိနိုင်ပါသည်။&nbsp;</p>
                <p>&nbsp; &nbsp; မွေးဖွားချိန်ရှိ ကိုယ်ခန္ဓာအနေအထားသည် အရေးကြီးပါသည်။ အကယ်၍ ဝလွန်းနေလျှင်
                    မွေးဖွားချိန်တွင် မွေးရခက်ခြင်းနှင့် ဇီဝကမ္မချို့ယွင်းမှုရောဂါများ ဖြစ်နိုင်သည်။ အကယ်၍
                    ပိန်လွန်းနေလျှင် မွေးပြီးနောက် နို့များများထုတ်လုပ်ရန်နှင့် နောက်ထပ်သားမြန်မြန်ရရန် ရုန်းကန်ရမည်။
                    မွေးဖွားချိန်ရှိ ကိုယ်ခန္ဓာအနေအထားသည် ပုံတွင်ပြထားသည့် အတိုင်းဖြစ်သင့်သည်။</p>
                <p>&nbsp; &nbsp; နွားမနို့နားခါနီးတွင် မှန်ကန်သည့် ကိုယ်အလေးချိန်ရှိစေရန်နှင့် နို့နားကာလတစ်လျှောက်လုံး
                    ထိုအတိုင်းရှိနေအောင် ထိန်းထားရမည်။ ကိုယ်ဝန်ဆောင်ကာလအလယ်ပိုင်းတွင် နွားမကို ကိုယ်အလေးချိန်တက်အောင်
                    ပြုလုပ်ရခြင်းသည် ကိုယ်ဝန်ဆောင်ကာလနောက်ပိုင်းတွင် ပြုလုပ်ရခြင်းထက် လွယ်ကူသည်။
                    ကိုယ်ဝန်ဆောင်ကာလနောက်ပိုင်းတွင် အစာအိမ်ဘေးရှိ နွားကလေးသည် ဝမ်းဗိုက်အတွင်းနေရာ ပိုယူလာသည့်အတွက်
                    အစာအိမ်၏နေရာ ကျဉ်းသွားပြီး အစာပမာဏများများ မစားနိုင်တော့ပေ။ ဝမ်းဗိုက်အတွင်းရှိ နွားကလေးသည်
                    ကြီးထွားရန်အတွက် စွမ်းအင်ဓာတ်အများကြီး အသုံးပြုရပြီး ထိုစွမ်းအင်ဓာတ်ကို မိခင်ဆီမှ ရယူသုံးစွဲသည်။</p>
                <p>&nbsp; &nbsp; နွားမကြီးသားမွေးပြီးနောက် နို့စပေးချိန်တွင် ၎င်း၏ခန္ဓာကိုယ်မှ စွမ်းအင်ဓာတ်နှင့်
                    အသားဓာတ်ကို နို့အတွင်းသို့ များစွာထည့်ပေးရသည်။ ထို့ပြင် အရိုးအတွင်းမှ ကယ်လ်စီယမ်ဓာတ်ကို
                    နို့အတွင်းသို့ ထည့်ပေးရသေးသည်။ ဒါဟာ နွားကလေးမွေးပြီးနောက် နို့ရောင်ဖျား၊ ကီတိုးဆစ်ကဲ့သို့သော
                    ဇီဝကမ္မချို့ယွင်းမှုရောဂါများ ဖြစ်လာစေနိုင်သည်။ နို့ရောင်ဖျားရောဂါသည် နွားကလေးမွေးပြီးနောက်
                    နွားမကြီးတွင် ကယ်လ်စီယမ်ဓာတ် လုံလောက်မှုမရှိခြင်းကြောင့် ဖြစ်သည်။ ကီတိုးဆစ်ရောဂါသည်
                    ခန္ဓာကိုယ်အလေးချိန် အလွန်မြန်မြန်ကျသွားခြင်းကြောင့်ဖြစ်သည်။ ဤရောဂါများသည် ထုတ်လုပ်မှုကောင်းသော
                    နွားမများတွင် ပိုအဖြစ်များသည်။</p>
                <p>&nbsp; &nbsp; နို့ရောင်ဖျားရောဂါ၏ လဏ္ခာများမှာ အစာစားနှုန်းကျခြင်း၊ အားနည်းခြင်း၊ တဖြည်းဖြည်း
                    မထနိုင်တော့သည်အထိ လဲကျသွားခြင်း တို့ဖြစ်သည်။ ဤရောဂါသည် မွေးပြီး ပထမပတ်တွင် ဖြစ်ပွားသည်။
                    မွေးဖွားချိန်တွင် နို့ရောင်ဖျားရောဂါဖြစ်ပွားမှုကို လျှော့ချရန် မမွေးခင် နို့နားကာလတွင် အစာ၌
                    ကယ်လ်စီယမ်ထည့်ကျွေးခြင်းမျိုး ရှောင်ရမည်။ မမွေးခင်ကာလတွင် လုံလောက်သည့် ကယ်လ်စီယမ်ရရှိအောင်
                    နွားမ၏ခန္ဓာကိုယ်မှ ၎င်းကိုယ်တိုင် ပြုလုပ်မည်ဖြစ်သည်။ နွားကလေးမွေးပြီးသည်နှင့် ကယ်လ်စီယမ်
                    ဖြည့်စွက်ထည့်ကျွေးရမည်။ နို့ပေးနွားမများကို DCP (Di-Calcium Phosphate) အမှုန့်ကျွေးနိုင်သည်။
                    နို့နားနွားမများကို ကျွေးရန်မလိုပါ။ အကယ်၍ နို့ရောင်ဖျားရောဂါပြဿနာရှိနေသေးလျှင် မမွေးခင် ၂ - ၃ ပတ်
                    အလိုတွင် မဂ္ဂနီစီယမ် ကျွေးပေးရမည်။ မွေးပြီးနောက်တွင် ရက်သတ္တပတ်အနည်းငယ်အထိ မဂ္ဂနီစီယမ်နှင့်
                    ကယ်လ်စီယမ်ကို ရောကျွေးပေးရမည်။ မက်ဂနီစီယမ်သည် ခန္ဓာကိုယ်မှ ကယ်လ်စီယမ်စုပ်ယူခြင်းကို အားပေးသည်။</p>
                <p>&nbsp; &nbsp; နောက်ပြဿနာတစ်ခုသည် နို့အုံရောင်ရောဂါဖြစ်သည်။ မမွေးခင် ၁ ပတ်အလိုခန့်တွင် နွားမ၏
                    နို့အုံသည် စတင်ကြီးကြီးလာသည်။ နို့တိုင်ရှိ နို့ပေးပြွန်လေးများ အနည်းငယ်ပွင့်လာပြီး နို့တိုင်အတွင်း
                    နို့များဖြင့် ညှစ်ပတ်စေသည်။ ထို့ကြောင့် ဘက်တီးရီးယားပိုးများ နို့တိုင်အတွင်း စတင်ပေါက်ဖွားလာပြီး
                    နို့အုံရောင်ရောဂါ ဖြစ်စေနိုင်သည်။ အကယ်၍ သင့်ခြံတွင် သားမွေးပြီးစနွားမများတွင်
                    နို့အုံရောင်ရောဂါဖြစ်တတ်လျှင် မမွေးခင် ၁ ပတ်တွင်းမှစ၍ နို့တိုင်များကို အိုင်အိုဒင်းဆေးရည်ဖြင့်
                    နှစ်ပေးရမည်။ နွားခြံအခင်းကို သန့်ရှင်းအောင်ထားရမည်။ နို့တိုင်များကို အိုင်အိုဒင်းဆေးရည်ဖြင့် ၁ နေ့ ၁
                    ကြိမ် နှစ်ပေးလျှင် နို့တိုင်များရှိ ဘက်တီးရီးယားပိုးများကို သတ်ပေးပြီး နို့အုံရောင်ရောဂါမဖြစ်အောင်
                    ကာကွယ်ပေးနိုင်မည်။</p>
                <p>&nbsp; &nbsp; မွေးခါနီး ၁ - ၂ ပတ် အလိုတွင် နွားမကို အစာအလွန်အများ မကျွေးသင့်ပါ။ မှန်ကန်သည့်
                    ကိုယ်ခန္ဓာအနေအထားရအောင်သာ ထိန်းကျွေးသင့်သည်။ မွေးပြီးနောက် ကျွေးမည့် အစာနုပမာဏကို မမွေးခင် ၁ -၂ ပတ်
                    အလိုမှစတင်၍ ဖြည်းဖြည်းချင်း တိုးကျွေးသွားရမည်။ အစာနုပမာဏ အပြောင်းအလဲမြန်လျှင် အဆီတိုးဆစ်ရောဂါ
                    ဖြစ်နိုင်သည့်အတွက် ဖြည်းဖြည်းချင်းတိုးသွားခြင်းသည် ပိုကောင်းသည်။ အကြမ်းဖျင်းအားဖြင့် အစာနုကို ၁
                    နေ့လျှင် ၅၀ ကျပ်သား ထက် တိုးမကျွေးသင့်ပေ။ မမွေးခင် အစေ့အဆံအစာအကျွေးများလျှင် နို့မန်းတက်သည့်ပြဿနာ
                    ဖြစ်နိုင်သည်။ အကယ်၍ ခြံတွင် နို့မန်းတက်သည့်ပြဿနာဖြစ်တတ်လျှင် မမွေးခင်ကာလ အနည်းငယ်ခန့်တွင်
                    အစာနုကျွေးသည့် ပမာဏကို လျှော့ချသင့်သည်။&nbsp;</p>
                <p>&nbsp; &nbsp; လတ်တလော သားမွေးပြီးစနွားမများကို အရည်အသွေးကောင်းသည့် အစာစိမ်းကို ကျွေးနိုင်သမျှ
                    များများကျွေးပေးရမည်။&nbsp; မွေးပြီးပထမပတ်အတွင်းတွင် အရည်အသွေးအကောင်းဆုံး အစာစိမ်းကို စားချင်သမျှ
                    စားနိုင်အောင် ကျွေးပေးဖို့ကြိုးစားသင့်သည်။ နို့ပေးကာလအစောပိုင်းတွင် နို့ထွက်နှုန်းမြင့်လေလေ
                    နို့ပေးကာလတစ်လျှောက်လုံး၏ နို့ထွက်နှုန်းမြင့်လေဖြစ်သည့်အတွက် ထိုနွားမမျိုးကို
                    ကောင်းစွာကျွေးနိုင်ဖို့ အလေးထားသင့်သည်။</p>
