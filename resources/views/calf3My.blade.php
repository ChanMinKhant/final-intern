                    ကာကွယ်ဆေးထိုးနှံမှုများ သေချာလုပ်ဆောင်ရမည်။ နွားကလေးခြံတစ်ခြံထဲတွင် အရွယ်တူနွားကလေးများသာ ထားရမည်။
                    နို့ဖြတ်ပြီးသည်နှင့် နွားငယ်ကို သန့်ရှင်းအေးမြသောရေ၊ စဉ်းထားသော အစာစိမ်း၊ အစာနုတို့ကို အမြဲစားနိုင်အောင်
