                <p> During pregnancy, the umbilical cord carries the oxygen and nutrients necessary for the fetus to survive
                    and grow from the mother cow to the fetus, and carry out waste products from the fetus. </p>
                <p> After the calf's umbilical cord is cut, the remaining part naturally closes, dries and falls off.
                    However, before the umbilical cord dries, due to lack of cleanliness, surrounding bacteria may enter and
                    cause umbilical inflammation and abscesses. It happens until the fly falls. </p>
                <p>Sometimes due to difficulty in giving birth, the umbilical cord is torn at the time of birth, the
                    umbilical cord is too short, etc.
                    In some small cows, due to the lack of cleanliness, it also happens that they hide each other with their
                    tongues. </p>
                <p> &nbsp; &nbsp; If the young cows develop stomach inflammation, they will have a fever and then they will
                    die. Instead, they will lose their appetite.
                    A calf with a swollen umbilicus should be isolated in a clean area. It should be properly treated with
                    an understanding person. If there is an abscess, it must be expelled. </p>
                <p> If the calf's umbilical cord is still wet after birth, it can become infected. Sterilizing the umbilical
                    cord by dipping it in iodine solution can prevent infection.
                    You can use the kind of iodine that is used to dip cows' teats after milking.</p>
                <p> If you are milking the calf, there is no benefit in letting the calf go with the cow. If it is separated
                    from the mother when it is still young, it will cause more discomfort for the calf.</p>
                <p> In addition, most of the bacteria that enter the body through the umbilical cord stay in the joints and
                    may cause arthritis. </p>
                <p> Dairy cows that have colic problems at a young age are unable to produce the maximum
                    production they really could. </p>
                <p> Colitis is the fourth most common disease in calves. Therefore, proper precautions should
                    be taken to prevent colic inflammation in calves. </p>
                <p> When a calf is born, there are habits that we must do in the first week of its life.</p>
                <p> <span style="color: #008080;"><b>To prevent calf inflammation</b></span></p>
