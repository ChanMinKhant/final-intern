                <p class="text-center"> <b> Chlorisgayanacv Callide “Rhodes grass”</b> </p>
                <p> - Rhodes grass can grow in any soil. However, it thrives well in sandy soil.
                    It can grow in acidic soils, but the output rate is not high.
                    It can be grown in salty soil.</p>

                <p> - Less productive in humid areas.
                    Rainfall (less than 700 mm) and the ability to withstand prolonged dryness.</p>

                <p> - Good yield if planted in areas with access to water.
                    Excellent for fallow pasture planting and can control weeds.
                    It can spread along the ground.</p>

                <p> - Not suitable for planting under shady plants.
                    It is necessary to let the cattle out, harvest and transport them regularly.
                    If the harvesting time is late, the loss of nutritional value is fast.</p>
