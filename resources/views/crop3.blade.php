                <p class="text-center"> <b> Setariasphacelatavarsplendida cv Splenda “Splenda Setaria”</b> </p>
                <p> - Splenda Setaria grass is a type of grass that can be grown on any type of soil. </p>
                <p> - High productivity in fertile soils.</p>
                <p> - Can be grown in wet and waterlogged areas.
                    Not suitable for planting in shady areas.</p>
                <p> - Tolerant of drought, but yield declines during drought.</p>
                <p> - Easy to harvest. It can be used to make hay and pickles.</p>
                <p> - Cultivation of other types of grasses, legumes such as stilts, pea plants
                    Can be mixed. Can be planted. </p>
