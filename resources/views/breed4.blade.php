                <p>Making birth easier by managing cows well at the time of birth. being healthy after birth; High milk
                    yield etc. can be obtained in early lactation period.&nbsp;</p>
                <p> Body position at birth is important. If it is too fat, it can cause birth defects and birth defects. If
                    you are too thin, you will struggle to produce more milk after birth and have another baby sooner.
                    The position of the body at the time of birth should be as shown in the picture.</p>
                <p> Heifers should be at the correct weight at weaning and maintain that weight throughout the weaning
                    period.
                    Getting a heifer to gain weight in mid-pregnancy is easier than doing it later in pregnancy.
                    Later in pregnancy, the stomach side of the calf takes up more space in the abdomen, causing the stomach
                    space to narrow and not be able to eat large amounts of food. A calf in the womb needs a lot of energy
                    to grow, and it gets that energy from the mother.</p>
                <p>When a heifer starts giving milk after calving
                    Its body has to put a lot of energy and protein into the milk. In addition, the calcium from the bones
                    must be added to the milk. This is milk fever after the birth of a calf. It can cause physiological
                    disorders such as ketoacidosis. Milk fever is caused by insufficient calcium intake in heifers after
                    calving. Ketosis is caused by a very rapid loss of body weight. These diseases are more common in cows
                    with good production.</p>
                <p>Symptoms of milk fever are loss of appetite, weakness Gradually falling down until unable to stand up.
                    This disease occurs in the first week after birth.
                    To reduce the incidence of milk fever at birth, calcium should be avoided during the lactation period
                    before birth. The heifer's body will make it on its own to get enough calcium before calving. Calves
                    should be supplemented with calcium once they are born. Lactating cows can be fed DCP (Di-Calcium
                    Phosphate) powder.
                    Dairy heifers do not need to be fed. If milk fever is still a problem, magnesium should be given 2-3 weeks
                    before calving. A mixture of magnesium and calcium should be fed for a few weeks after birth. Magnesium
                    promotes the absorption of calcium by the body.</p>
                <p> Another problem is mastitis. About 1 week before calving, the heifer's udder begins to grow. The milk
                    supply ducts in the teat are slightly opened and the milk is squeezed around the teat. As a result,
                    bacteria begin to grow in the breast and can cause mastitis. If mastitis occurs in heifer that have just
                    given birth on your farm, you must bathe the teats with iodine solution 1 week before calving. The
                    cattle yard must be kept clean. Bathing the nipples with iodine solution once a day will kill the
                    bacteria in the nipples and prevent mastitis.</p>
                <p>The heifer should not be fed too much food 1-2 weeks before calving. It should be fed only to get the
                    correct body position. The amount of soft food to be fed after birth must be gradually increased
                    starting 1-2 weeks before birth. If the amount of small food changes too quickly, it is better to
                    gradually increase it because it can lead to obesity. Roughly speaking, feed should not exceed 50 Kyats
                    per day. If you feed them seeds before birth, there may be a problem with milk supply. If there is a
                    problem with milk production on the farm, the amount of soft feed should be reduced for a few days
                    before calving.&nbsp;</p>
                <p>Recent calving heifers should be fed as much good quality raw forage as possible.&nbsp; During the first
                    week after birth, you should try to feed him as much raw food as he wants. The higher the milk yield at
                    the beginning of the lactation period, the higher the milk yield throughout the lactation period.
