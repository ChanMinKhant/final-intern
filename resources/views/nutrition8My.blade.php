                    စားကျက်​စိုက်​ဖို့ ကြိုးစားလာကြတယ်​။ တစ်​ချို့က ကိုယ်​တတ်​နိုင်​သ​လောက်​ ​မြေငှါးပြီး နှစ်​တို (သို့)
                    နှစ်​ရှည်​စားကျက်​စိုက်​ဖို့ ကြိုးစားလာကြတယ်​။ တစ်​ချို့ ​မြေအနည်းငယ်​ပဲရှိသူ​တွေက
