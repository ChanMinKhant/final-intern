            <p style="padding-top: 20px;"> &nbsp; &nbsp; မိမိရဲ့နွားက ပိန်နေပြီး မြိုင်လက္ခဏာမပြဘဲဖြစ်နေလျှင်
                အဓိကအကြောင်းရင်းကတော့ သူမက
                အစာလုံလောက်စွာမစားရခြင်း (သို့) သူမရဲ့အစာထဲမှာ အာဟာရလုံလောက်စွာ မပြည့်မီခြင်းကြောင့် ဖြစ်သည်။</p>
            <p>&nbsp; &nbsp; နွားမအတွက် စွမ်းအင်ဓာတ် အပြည့်အဝလုံလောက်သော်လည်း ရေ၊ အသားဓာတ်နှင့် သတ္တုဓာတ်များလည်း
                ပြည့်မီဖို့လိုအပ်ပါသည်။</p>
            <p>&nbsp; &nbsp; မြန်မာနိုင်ငံရဲ့ မွေးမြူမှုဓလေ့အရ နို့စားနွားမွေးမြူရေးတွင်
                ရေလုံလောက်စွာမသောက်သုံးရခြင်း ကြုံတွေ့နေရပါသည်။ ဒါကြောင့် သန့်ရှင်းသောရေကို အကန့်အသတ်မရှိ
                စိတ်ကြိုက်သောက်သုံးနိုင်အောင် စီမံထားသင့်ပါသည်။</p>
            <p>&nbsp; &nbsp; ကောက်ရိုးကျွေးနေသောနွားမများတွင် အသားဓာတ်မပြည့်မီခြင်း ဖြစ်နိုင်ပါတယ်။
                အရည်သွေးကောင်းသော အစာစိမ်းတွင် အသားဓာတ်အသင့်အတင့်ပါဝင်ပြီး အဆီကြိတ်ဖတ်များ၊ ပဲအမျိုးမျိုးမှရရှိသော
                အစာနုများဖြစ်သည့် မြေပဲဖတ်၊ ကုလားပဲမှုန့် စသည်တို့တွင် အသားဓာတ်များစွာပါဝင်သည်။</p>
            <p>&nbsp; &nbsp; တစ်ခါတရံ နို့ပေးနွားမများတွင် သတ္တုဓာတ်လိုအပ်ချက်များလည်း ကြုံရနိုင်ပါသည်။ ၎င်းတို့ဟာ
                မျိုးပွားခြင်းမှာသက်ရောက်စေတဲ့ အခြားအကြောင်းရာတွေလောက် အရေးပါဝင်မှု နည်းပါသည်။
                သတ္တုဓာတ်လိုအပ်ချက်ပြဿာနာများ ကြုံတွေ့ရပါက သတ္တုလျက်တုံးများ၊ သတ္တုထိုးဆေးများ၊ ကယ်လ်ဆီယမ်နှင့်
                ဖေါ့စပရပ် ပါဝင်သော (DCP)ကို အစာထဲတွင် ဖြည့်စွက်ကျွေးခြင်းများ ဆောင်ရွက်နိုင်ပါတယ်။
                နီးစပ်ရာတိရစ္ဆာန်ဆေးကုဆရာဝန်ကို မိမိနွားမရဲ့အခြေအနအားပြောပြပြီး သင့်လျော်သော ကုသခြင်း၊
                ဆောင်ရွက်ခြင်းများ ပြုလုပ်သင့်ပါတယ်။</p>
