                <p>&nbsp; &nbsp; နွားမတစ်ကောင် တစ်သားမွေးပြီးနောက် (၂-၃) လအတွင်း သားပြန်မတောင်းရခြင်းရဲ့
                    အဓိကအကြောင်းအရင်းမှာ အာဟာရလုံလောက်မှုမရှိခြင်း (ချို့တဲ့ခြင်း) ကြောင့် ဖြစ်သည်။</p>
                <p>&nbsp; &nbsp; နွားမရဲ့ခန္ဓာကိုယ်ဟာ ဘယ်အချိန်မှာ သူ့အတွက် ကိုယ်ဝန်ဆောင်ဖို့အသင့်တော်ဆုံးလဲ ဆိုတာ
                    အလိုအလျောက်သိထားပြီး ဖြစ်ပါတယ်။ အကယ်၍ နွားမတစ်ကောင်ဟာ အစာအာဟာရ လုံလောက်စွာမရရှိတဲ့အခါ နောက်တစ်ကြိမ်
                    ကိုယ်ဝန်ပြန်ဆောင်ဖို့ အဆင်သင့်မဖြစ်သေးဘူးဆိုတာ သိနေပါတယ်။ နွားမတစ်ကောင်ဟာ ၎င်းတို့ခန္ဓာကိုယ်ထဲမှာ
                    နွားကလေးအတွက် ကိုယ်ဝန်ဆောင်ဖို့ အဆင်သင့်ဖြစ်တဲ့အချိန်မှာ သားတောင်းတဲ့ လက္ခဏာစပြပါတယ်။</p>
                <p>&nbsp; &nbsp; နို့စားနွားမရဲ့ အစာထဲမှာ စွမ်းအင်ဓာတ်၊ အသားဓာတ်၊ သတ္တုဓာတ်နှင့် ရေ လုံလောက်ခြင်းမရှိပါက
                    နွားမသည် မျိုးပွားခြင်းအတွက် လုပ်ငန်းမစတင်နိုင်ပါဘူး။ ဆိုလိုတာက နွားမရဲ့ သားဥအိမ်မှ
                    သားဥမကြွေသည့်အတွက် မြိုင်လက္ခဏာမပြခြင်း ဖြစ်သည်။</p>
                <p>&nbsp; &nbsp; နို့စားနွားမတွေအတွက် အဓိကလိုအပ်ချက်ဖြစ်နိုင်သော အာဟာရကတော့ စွမ်းအင်ဓာတ်ပဲဖြစ်ပါတယ်။
                    စွမ်းအင်ဓာတ်မလုံလောက်ပါက နွားမသည် တဖြည်းဖြည်းပိန်လာမယ်။ ပိန်နေတဲ့နွားမတစ်ကောင်အတွက်
                    သားပြန်တောင်းဖို့ကြာပါတယ်။ အစာဟာရ ပြည့်ဝစွာစားသုံးနေရသော နွားမတစ်ကောင်ဟာ မွေးပြီး၂-၃ လ အကြာတွင်
                    သားပြန်တောင်းပါမယ်။&nbsp;</p>
                <p>&nbsp; &nbsp; နို့ပေးကာလအစောပိုင်းနွားမကို အရည်သွေးကောင်းသော အစာစိမ်းများ စားနိုင်သလောက်
                    စိတ်ကြိုက်စားနိုင်အောင် ကျွေးမွေးသင့်ပြီး အကယ်၍ နွားမပိန်နေပါက အစာနုများဖြည့်စွက်
                    ကျွေးမွေးဖို့လိုအပ်ပါတယ်။</p>
                <p>&nbsp; &nbsp; အောက်မှာဖော်ပြထားသော ပုံ(၂) ပုံ မှာ ပထမပုံမှ နွားမသည် ပိန်းလွန်းနေပြီး
                    ဒုတိယပုံထဲမှနွားမသည် သားစပ်ဖို့ သင့်တော်တဲ့ခန္ဓာကိုယ်အနေအထားပဲဖြစ်ပါတယ်။
