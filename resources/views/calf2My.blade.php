                <p> &nbsp; &nbsp; နို့ဘူးဖြင့်နို့တိုက်ကျွေးခြင်းဖြင့် လိုအပ်သောနို့ပမာဏကို တိကျစွာ တိုက်ကျွေးနိုင်ပါသည်။
                    မိခင်ဆီမှနို့ကို တိုက်ရိုက်သောက်ရသောအခါ နွားငယ်လေးရရှိသော နို့ပမာဏကို သိရန် ခက်ခဲပါသည်။ လုံလောက်သော
                    နို့ပမာဏမရရှိပါက
                    ကောင်းမွန်စွာကြီးထွားမှာ မဟုတ်သလို ဖျားနာမှုများလည်း ပိုဖြစ်နိုင်ပါသည်။</p>
                <p> &nbsp; &nbsp; နွားငယ်လေးသည် လိုအပ်သော နို့ပဏထက်ပိုရပါက ကောင်းမွန်စွာ ကြီးထွားမည်ဖြစ်သော်လည်း
                    ရောင်းနိုင်တဲ့ နို့ပမာဏလျော့သွားမည်ဖြစ်ပါသည်။ နို့ဘူးဖြင့် နို့တိုက်ကျွေးခြင်းကို ခြံ၏အပြင်ကနေ
                    လုပ်ဆောင်နိုင်သလို
                    စီမံရပိုလွယ်ကူပြီး ၎င်းတို့ကို နေရာရွေ့ခြင်း အစာကျွေးခြင်းတို့ကို တစ်ကောင်ခြင်းလုပ်ရန် မလိုတော့ပါ။
                    ခြံသမားအများစုသည် နွားမကြီးနှင့် နွားကလေးကို ခွဲထားရန် မကြိုက်ကြပါ။ သို့သော် ဤကဲ့သို့ခွဲထားခြင်းသည်
                    နွားငယ်လေးအပေါ်တွင် ထိခိုက်မှုမရှိပါ။ နွားကလေးသည်လည်း နွားမကြီးထက် များသောအားဖြင့် နို့ကိုသာ
                    ဂရုစိုက်တတ်ကြပါသည်။ နွားမကြီးနှင့် နွားကလေးကို ခွဲထားသောအခါ ၂ ရက်မှ ၃ ရက်အထိသာ စိတ်မကောင်းဖြစ်တတ်ကြပြီး
                    ၎င်းတို့သည် အလွန်လျှင်မြန်စွာ မေ့သွားကြပါသည်။ နွားကလေးအတွက် နို့တိုက်ကျွေးသူသာ အမေအသစ်အဖြစ် စိတ်ထဲတွင်
                    ထင်မှတ်လာပါသည်။</p>
                <p> &nbsp; &nbsp; အောက်ပါပုံရှိ နွားငယ်လေးနှစ်ကောင်တွင် တစ်ကောင်သည် ဖရီရှန်မျိုး အသက်ငါးလအရွယ်ဖြစ်ပြီး
                    နို့ဘူးဖြင့် နို့တိုက်မွေးခဲ့ပြီး တစ်ကောင်သည် လေးလအရွယ် ဒေသနွားမျိုးဖြစ်ပြီး မိခင်မှနို့ကို
                    တိုက်ရိုက်တိုက်ကျွေးထားခြင်း ဖြစ်ပါသည်။ နို့ဘူးဖြင့် တိုက်ကျွေး‌ထားသော နွားလေးက များစွာပို၍
                    ကောင်းမွန်သည်ကို တွေ့ရပါသည်။ ထို့ကြောင့်
                    နွားငယ်လေးများကို မိခင်နှင့် ခွဲထားခြင်းကို မစိုးရိမ်သင့်ပါ။</p>
                <p> &nbsp; &nbsp; အကယ်၍ သင်သည် နွားငယ်လေးများအတွက် ဂရုစိုက်မည်ဆိုလျှင် နွားငယ်လေးများကို ခွဲထားဖို့
                    စဉ်းစားရန်လိုပါသည်။ နွားငယ်လေးများသည် လုံလောက်သောနွားနို့၊ သက်တောင့်သက်သာရှိသော လွတ်လပ်စွာနေနိုင်သော
                    ခြံ၊ ရေနှင့် အစာတို့လိုအပ်ပါသည်။ နွားငယ်လေးကို နွားမကြီးအနီးနားတွင် တစ်နေ့လုံး ကြိုးချည်ထားခြင်း သည် ၎င်းအတွက်
                    မပျော်‌ရွင်စေနိုင်ပါ။ နွားငယ်သည်လည်း လွတ်လပ်စွာ လှည့်ပတ်သွားရန် လိုအပ်ပါသည်။</p>
                <p> &nbsp; &nbsp;သင်၏ နွားငယ်လေးများကို မွေးစမှ အသက် သုံးလခန့် (ခန္ဓာကိုယ်အလေးချိန် ၁၀၀ ကီလိုဂရမ် ခန့်အထိ)
                    တစ်နေ့လျှင် နွားနို့ ၃ ပိသာတိုက်ရန် အကြံပေးလိုပါသည်။ နွားငယ်လေးများ ကြီးလာသောအချိန်တွင် တစ်ခါသောက်လျှင် ၃
                    ပိဿာထက်ပိုပြီး သောက်နိုင်ကြပါသည်။ ဤအခြေအနေတွင် တစ်နေ့တစ်ခါ တိုက်ခြင်းသည် အဆင်ပြေသော်လည်း အသက်တစ်ပတ်သားအောက်
                    နွားငယ်လေးများတွင် တစ်နေ့ နှစ်ကြိမ်တိုက်ကျွေးခြင်းက ပိုကောင်းပါသည်။ နွားငယ်လေးကြီးထွားလာသည်နှင့်
                    နောက်တစ်ကြိမ် အစာမလာမီ ဆာလောင်တတ်လာပြီး အစာခဲများကို စားရန်ကြိုးစားလာကြပါသည်။ ဤအရာ‌သည်
                    ကောင်းသောလက္ခဏာဖြစ်ပြီး ၎င်းကိုအစာခဲများ စားရန် အကျင့်လုပ်ပေးနိုင်ခြင်းသည် သားခွဲရာတွင် အဆင်သင့်
                    ဖြစ်စေပါသည်။</p>
                <p> &nbsp; &nbsp; နွားနို့ဘူးများကို သန့်ရှင်းစွာ ထိန်သိမ်းရန်လည်း အလွန်အရေးကြီးပါသည်။ အဘယ်ကြောင့်ဆိုသော်
                    အရေးကြီးဆုံးတစ်ချက်မှာ နို့ဘူးများမသန့်ရှင်းပါက နွားငယ်လေး ၀မ်းလျော၀မ်းပျက်ဖြစ်နိုင်သောကြောင့် ဖြစ်သည်။
                    နို့ဘူးများကို ရေနွေး ၊ ဆပ်ပြာရည်တို့ဖြင့် သန့်ရှင်းစွာ ဆေးကြောရန်လိုအပ်ပါသည်။</p>
                <p> &nbsp; &nbsp; ခြံသမားများအတွက် နို့ဘူးဖြင့်တိုက်ခြင်းသည် အချိန်ပိုကုန်နိုင်သည်ဟူသော အချက်ရှိပါသည်။
                    ဤအရာသည် တချို့ခြံများအတွက် မှန်ပါသည်။ သို့သော် တချို့ခြံများတွင်တော့ နွားငယ်လေးများကို နေရာရွေ့ပြောင်းစရာ
                    မလိုသည့်အတွက် အမှန်တကယ် အချိန်ကုန်သက်သာတယ်သည်ကို တွေ့ရှိပြီးဖြစ်ပါသည်။</p>
                <p> &nbsp; &nbsp; နွားကလေးများ ငယ်ရွယ်ချိန်တွင် နို့ဘူးကို ကိုင်ပြီးတိုက်ရန် လိုသော်လည်း ကြီးလာသောအခါ
                    နို့ဘူးထည့်သော အရာပြုလုပ်ပြီးတိုက်ခြင်းဖြင့် လူမှ ကိုင်၍ တိုက်ရန်မလိုအပ်တော့ပေ။ နို့ဘူးထည့်စရာ
                    ပြုလုပ်ထားပါက နွားငယ်လေးအကုန်သောက်၍ မပြီးခင် ပြန်လာယူရန်လိုအပ်ပါသည်။ အဘယ်ကြောင့်ဆိုသော် လေကို အကြာကြီး
                    စုပ်နေခြင်းက ၎င်းအတွက် မကောင်းသောကြောင့်ဖြစ်ပါသည်။</p>
                <p> &nbsp; &nbsp; ပုံးအပွင့်ဖြင့်လည်း တိုက်ရိုက်တိုက်နိုင်ပါသည်။ သို့သော် ဘူးဖြင့်တိုက်ခြင်းကို
                    အကြံပေးရခြင်းမှာ နွားငယ်လေးသည် မိခင်မှကဲ့သို့ စုပ်သောက်ခြင်းကို ကြိုက်ပြီး ၎င်းတို့၏ အစာချေမှုအတွက်လည်း
                    ပိုကောင်းပါသည် ။ ထို့အပြင် လေဖောင်းခြင်းနှင့် နို့ကို အဆုတ်ထဲသို့ မျိုချမိခြင်း တို့ကိုလည်း နည်းစေပါသည်။
                    ထိုကဲ့သို့ တူညီသော အကြောင်းပြချက်များကြောင့် နို့ဘူး၏ နို့သီးခေါင်းတွင် နွားငယ်လေးစုပ်ရန် အပေါက်ဖောက်ရာတွင်
                    အပေါက်သေးသေးတစ်ခုသာဖောက်ရန် အကြံပြုပါသည်။</p>

