                <p> Milk feverသည် Calciumဓာတ် မလုံလောက်မှုကြောင့်ဖြစ်သော ဇီဝကမ္မဗေဒလုပ်ငန်းဆိုင်ရာ
                    ချို့ယွင်းမှုအရဖြစ်သည့်ရောဂါဖြစ်သည်။
                    Milk feverသည် အခြားသောရောဂါများနှင့်လည်း ဆက်စပ်ဖြစ်ပွားနိုင်သည်။ </p>
                <p> <span style="color: #008080;"><b>ဖြစ်ပွားရသည့်အကြောင်းအရင်း</b></span></p>
                <p> ကိုယ်ဝန်ဆောင်နောက်ပိုင်းကာလ၊ မွေးဖွားခြင်း၊ နို့ဦးရည်ထုတ်လုပ်ခြင်း၊
                    နို့ဦးရည်ထုတ်ခြင်းနှင့် နို့ပေးကာလအစောပိုင်း စသည်တို့ကြောင့် Calcium လိုအပ်ချက်မြင့်လာပါသည်။ သို့သော်
                    ထိုအချိန်တွင် နွားမသည် ၎င်း၏ အရိုးမှ Calcium ကိုပြောင်းလဲသုံးစွဲနိုင်စွမ်းနှင့် အစာမှရရှိသည့် Calcium
                    ကို ကောင်းစွာစုပ်ယူနိုင်စွမ်း စသည့်တို့ လျော့နည်းနေချိန်လည်းဖြစ်သည်။ တချို့နွားများသည် လိုအပ်တဲ့ Calcium
                    ဓာတ်ကို လုံလုံလောက်လောက်ပြန်အစားမထိုးနိုင်ဘဲ Milk Fever ဖြစ်လာပါသည်။ အထူးသဖြင့် ထုတ်လုပ်မှုမြင့်တဲ့
                    နွားများတွင် ပိုအဖြစ်များပါသည်။</p>
                <p> Milk Feverကို မွေးခါနီးနှင့် မွေးပြီး ၃ ရက်ပတ်လည်ခန့်တွင် တွေ့ရများသည်။ သို့သော် နွားမ၏
                    နို့ပေးနိုင်မှုအရ အစာကျွေးမှုစနစ်နှင့် ဖြည့်စွက်ဓာတ်ကျွေးမှုပေါ် မူတည်ပြီး အခြားသောအချိန်များတွင်လည်း
                    ဖြစ်တတ်ပါတယ်။ </p>
                <p> မတမ်းများထက် သားမွေးများသည့် နွားမကြီးများတွင် ပိုအဖြစ်များသည်။ နွားမကြီးများသည် နို့ပိုထုတ်ရခြင်း၊
                    အသက်အရွယ်အရ သွေးတွင်း Calcium ကို လျှင်မြန်စွာ အစားထိုးနိုင်မှုအားနည်းခြင်း စသည်တို့ကြောင့်ဖြစ်သည်။ </p>
                <p> <span style="color: #008080;"><b>ရောဂါလက္ခဏာများ</b></span></p>
                <p> <span style="color: #008080;"><b>အဆင့် (၁)</b></span></p>
                <p> - အစာစားနှုန်းကျခြင်း</p>
                <p> - ဂနာမငြိမ်ခြင်း၊ တုံပြန်မှုမြန်ခြင်း</p>
                <p> - အားနည်းလာခြင်း</p>
                <p> - လမ်းလျှောက်ရာတွင် နောက်ခြေများယိုင်လာခြင်း</p>
                <p> <span style="color: #008080;"><b>အဆင့် (၂)</b></span></p>
                <p> - ခေါင်းစင်းလာခြင်း (သို့) ခေါင်းကိုခန္ဓာကိုယ်၏ ဘေးဘက်သို့လှည့်နေခြင်း (S ပုံစံလှည့်နေခြင်း)</p>
                <p> - နားရွက်ဖျားများ အေးစက်နေခြင်း</p>
                <p> - နှာခေါင်းဖျားများ ခြောက်နေခြင်း</p>
                <p> - လှမ်းလျှောက်ရာတွင် ဟန်ချက်မညီခြင်း</p>
                <p> - ကြွက်သားများတုန်ခြင်း</p>
                <p> - ဝမ်းချုပ်၊ ဆီးချုပ်ခြင်း၊ သာမန်ထက်အပူချိန်ကျခြင်း</p>
                <p> <span style="color: #008080;"><b>အဆင့် (၃)</b></span></p>
                <p> - မထနိုင်ဘဲလှဲနေခြင်း၊ အာရုံခံစားမှုအားနည်းလာခြင်း</p>
                <p>- နှလုံးခုန်နှုန်းမြန်လာခြင်း၊ နှလုံးခုန်သံတိုးလာခြင်း</p>
                <p> <span style="color: #008080;"><b>ကုသခြင်း</b></span></p>
                <p>Milk fever ဖြစ်နေသောနွားသည် ကုသမှုမရှိလျှင် သေဆုံးနိုင်သည့်အတွက် ကုသမှုကို မြန်နိုင်သမျှ မြန်မြန်ပေးရမည်။
                    Calcium (calcium gluconate, calcium borogluconate. . .) ထောက်ပံ့မှုပေးရမည်။
                <p> ရောဂါ ပြင်းထန်မှုအခြေအနေပေါ်မူတည်၍ Calciumကို <br>
                    - ပါးစပ်မှပေးခြင်း (Oral treatement) <br>
                    - အရေပြားအောက်သို့ပေးသွင်းခြင်း (Subcutaneous administration) <br>
                    - သွေးကြောထဲသို့ပေးသွင်းခြင်း (Intravenous injection) စသည်ဖြင့်ပေးနိုင်ပါသည်။
                </p>
                <p>သွေးကြောထဲသို့ပေးသွင်းရာတွင် သွေးခုန်နှုန်း/နှလုံးခုန်နှုန်းနှင့် ဆက်စပ်မှုရှိသောကြောင့်
                    တိရစ္ဆာန်ဆေးကုဆရာဝန်နှင့် ကုသသင့်သည်။ Magnesium သည်လည်း Calcium ထုတ်လုပ်မှုတွင် အရေးပါသည်။ </p>
                <p> <span style="color: #008080;"><b>ကာကွယ်ခြင်း</b></span></p>
                <p>နွား၏ ခန္ဓာကိုယ်အနေအထားသည် မဝလွန်း မပိန်လွန်းဖြစ်ရပါမည်။
                    ဝသောနွားသည် milk fever ဖြစ်နိုင်ခြေပိုများသည်။ </p>
                <p>နို့ပေးကာလတွင် နွားနို့ထုတ်ပေးနေရသောကြောင့် အစာမှ Calciumလုံလောက်စွာမရခြင်းကို ကာမိစေရန်အတွက် Di-calcium
                    phosphate (DCP)ကို အစာတွင်ထည့်ကျွေးရသော်လည်း နို့နားကာလတွင် DCP ကိုမကျွေးရပါ။ နို့နားကာလတွင်
                    နို့ထုတ်လုပ်ခြင်းမရှိသောကြောင့် Calcium လိုအပ်ချက်သည် နို့ပေးကာလလောက်မလိုအပ်ပေ။ </p>
                <p>နို့နားကာလတွင် DCP ကျွေးလျှင် ရရှိသည့် Calcium သည် လိုအပ်ချက်ထက်များနေသောအခါ အူမှ Calcium စုပ်ယူမှုနှင့်
                    ကိုယ်တွင်း Calcium ထုတ်လုပ်မှုလုပ်ငန်းစဉ်ကို လှုံ့ဆော်မှု အားနည်းသွားသည်။ နို့နားကာလမှ နွားကလေးမွေးပြီး
                    နို့ပေးကာလသို့ ရုတ်ချည်းကူးပြောင်းချိန်တွင် ထိုအချက်သည် ထိခိုက်မှုဖြစ်စေသည်။</p>
                <p>အကယ်၍ နွား၏ ခန္ဓာကိုယ်အခြေအနေနှင့် နို့ထွက်နှုန်းမြင့်စေချင်လျှင် နွားအဝလွန်တာလည်း မဖြစ်စေရန် energy
                    ကိုအခြေခံပြီး ကျွေးရမည်။</p>
                <p>နွားကလေးမွေးပြီးပြီးချင်းနှင့် နောက်ပိုင်းချိန်များတွင် Calcium feed supplements (DCP) ပေးခြင်းဖြင့်
                    အထောက်အကူဖြစ်စေသည်။ (ထိုအချိန်များမတိုင်ခင် စောစွာမပေးရပါ။ နို့နားကာလတွင် မပေးရပါ။
                    အစာကျွေးမှုကိုကောင်းစွာစီမံပါ။)</p>
