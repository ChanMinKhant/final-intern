                သားအောင်‌ခြင်း မအောင်ခြင်းတို့နဲ့ တိုက်ရိုက်ဆက်နွှယ်‌နေတယ်လို့ ယူဆ‌ကြတဲ့ သားအိမ်သန့်ခြင်း၊
