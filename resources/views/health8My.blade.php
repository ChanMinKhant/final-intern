                အလွယ်တကူပြောရရင် Metritis သားအိမ်ရောင်တာလို့လဲ
                ခေါ်နိုင်ပါတယ်။ </p>
            <p>- သားအိမ်မသန့်ဘူးဆိုတာ ဘယ်လိုမျိုးလဲ? </p>
            <p>- ဘယ်လို အခြေနေတွေက သားအိမ် မသန့်စေတာလဲ? </p>
            <p>- သားအိမ် မသန့်တဲ့ လက္ခဏာတွေကရော ဘာတွေလဲ? </p>
            <p>- သားအိမ်မသန့်တာကိုရော ဘယ်လို သိနိုင်လဲ? </p>
            <p>- သားအိမ် သန့်မသန့် ဘယ်အချိန်မှာ စစ်ဆေးလို့ရလဲ? </p>
            <p>- သားအိမ် ပုံမှန် Active ပြန်ဖြစ် မဖြစ် ဘယ်လိုစမ်းသပ်မလဲ? </p>
            <p>- မြိုင်မလာတာက သားအိမ်မသန့်တာနဲ့ တိုက်ရိုက်သက်ဆိုင်လား? </p>
            <p>- သားအိမ်မသန့်တာကရော သားအောင်ခြင်း၊မျိုးအောင်ခြင်းအပေါ် ဘယ်လိုသက်ရောက်မှုရှိလဲ? </p>
            <p>&nbsp; &nbsp; နို့စားနွားမတစ်ကောင်အတွက် သားအိမ်မသန့်ဘူး ဆိုတာ
                ပထမဆုံးစဉ်းစားရမဲ့အချက်က သားကျပြီး‌သော၊ကလေးမွေးပြီးသောနွားတွေမှာပဲ အတွေ့ရအများဆုံး ဖြစ်ပါတယ်။
                ပထမဆုံးသားစပ်မဲ့နွားမတမ်းတွေမှာ တွေ့ရခဲပါတယ်။ မရှိသလောက်ပါ။ </p>
            <p>&nbsp; &nbsp; နွားမ ကလေးမွေးတဲ့အချိန် အချင်းကပ်၊အချင်းကျန်၊မွေးဖွားရခက်တဲ့အခြေအနေများဟာ
                သားအိမ်မသန့်တာကို အဓိကဦးတည်စေတဲ့ အချက်ဖြစ်ပါတယ်။
                နောက်တစ်ခုအနေဖြင့် သားအိမ်အတွင်းမှာထိခိုက်ဒဏ်ရာရခဲ့ရင်လဲ သားအိမ်မသန့်တာ ဖြစ်တတ်ပါတယ်။
                သားအိမ်မသန့်ဘူးလို့ယူဆရတဲ့ အခြေအနေအများစုက ကလေးမွေးဖွားချိန် ကြုံတွေ့ရတဲ့ ပြဿနာတွေကြောင့်ပါပဲ။</p>
            <p>&nbsp; &nbsp; ဘယ်လိုလက္ခဏာတွေလဲ?
                အဓိက နွားမရဲ့ တန်ဆာကနေ ပုံမှန်မဟုတ်တဲ့ အချွဲတွေ ကျနေမယ်။
                အချွဲကျရင်ကော ကောင်းတဲ့အချွဲလား၊မကောင်းတဲ့အချွဲလား သိဖို့လိုပါတယ်။
                နွားမတစ်ကောင်မှာ ပုံမှန်ကျလေ့ရှိတဲ့အချွဲကိုတော့ သားအိမ်မသန့်ဘူးလို့ ယူဆလို့မရပါဘူး။
                ကိုယ့်နွားမ သားအိမ်သန့် မသန့် မွေးတဲ့အချိန်ကစပြီး
                သေချာ စောင့်ကြည့်စစ်ဆေးလို့ရပါတယ်။
                အချင်းကျန်နေရင်တော့ အကြီးမားဆုံး မသန့်ခြင်းပါပဲ။
                အချင်းကပ်နေလို့ အကုန်ခွာပြီးရင် သားအိမ်နံရံတွေကအနည်းငယ် ထိခိုက် ပျက်စီးနိုင်ပါတယ်။
                သို့သော် ပုံမှန်အားဖြင့် အချင်းကုန်ပြီဆိုရင်တော့ နွားမရဲ့သားအိမ်‌ခေါင်းကလဲပြန်ကျုံ့ပြီး သေးသွားပါမယ်။
                ထို့ကြောင့် အချိန်အနည်းငယ် ကြာပြီးမှ သား‌အိမ်ထဲကို လက်နဲ့နှိုက်ဖို့မလွယ်ကူတော့ပါ။
                ပုံမှန် အချင်းမကျန်တဲ့‌ကောင်ဆိုရင်တော့ မွေးပြီး အချင်းကျပြီးတာနဲ့ ပြန်ကျုံ့သွားပါမယ်။
                အများစုကတော့ အချင်းကျန်နေတဲ့အတွက် အစာစားနှုန်းကျမယ်။
                နို့ပေးတဲ့နွားမဆို နို့ထွက်နှုန်းကျမယ်၊ နောက်ဆက်တွဲ နို့အုံရောင်နိုင်မယ်။
                မွေးပြီး 2လလောက်အထိကို အချွဲတွေတွေ့ရတတ်ပါတယ်။
                ပုံမှန် သားအိမ် Activeပြန်ဖြစ်ဖို့ အချိန်ကြာပါမယ်။
                မွေးပြီး ပုံမှန်မြိုင်ပြန်လာဖို့ အချိန်ကြာပါမယ်။ </p>

            <p>&nbsp; &nbsp; မသန့်တာကိုရော ဘယ်လိုသိနိုင်လဲ?
                အဓိကအားဖြင့် မွေးပြီး 45 ရက်ဝန်းကျင်မှာ အထဲကိုစစ်ဆေးနိုင်ပါတယ်။
                ဒီအတိုင်း အချွဲတွေကျနေတာနဲ့ မသန့်ဘူးလို့မဆိုနိုင်ပါ။
                ပုံမှန် မြိုင်ပြန်မလာတာနဲ့ပဲ သားအိမ်မသန့်ဘူးလို့မဆိုနိုင်ပါ။
                သားအိမ်အတွင်းစစ်ဆေးပြီး သန့်နေပေမဲ့ တခြားအကြောင်းရင်းတွေကြောင့် မြိုင်မလာနိုင်တာရှိတတ်ပါတယ်။
                မြိုင်မလာတာ သားအိမ်မသန့်လို့ဆိုပြီး သားအိမ်သန့်ဆေးထိုးတာမျိုးတွေ ရှိကြမှာပါ။ ဒါပေမဲ့
                အချိန်မှန်ဖို့တော့လိုပါမယ်။
                မွေးပြီးတာနဲ့ သားအိမ်မသန့်တာကို မွေးမြူထားတဲ့ သူကိုယ်တိုင်က ပိုသိမှာပါ။
                အဲဒီအချိန်ကတည်းက ပုံမှန်ဆေးထိုး၊ဆေးတိုက်၊သားအိမ်ဆေးပေးလို့ရပါတယ်။</p>
            <p>&nbsp; &nbsp; အကယ်၍ ပုံမှန် မြိုင်လာတဲ့ အချိန်ကျမှ မြိုင်မလာလို့ သားအိမ်သန့်ဆေး သုံးရမယ်ဆိုပါစို့...
                သားအိမ်သန့်ဆေးဆိုတာ ဘယ်လိုဆေးမျိုးလဲ? ပဋိဇီဝဆေးတွေလား၊ ဟော်မုန်းဆေးတွေလား.....?
                အများသောအားဖြင့် ဟော်မုန်းဆေးနဲ့ပဲ အသုံးပြုရတာများပါတယ်
                ဒီလိုဟော်မုန်း‌ဆေးသုံးရတဲ့အခါမှာလဲ ဆေးရဲ့သဘာဝအရ သားအိမ်ထဲမှာရှိတဲ့မသန့်တာတွေကို သန့်စေဖို့ တစ်ခုထဲမဟုတ်ပဲ
                တခြားသောအာနိသင်နဲ့ ကောင်းကျိုးတွေလဲများစွာပါဝင်ပါတယ်
                အထူးသဖြင့် မြိုင်လာစေတဲ့ဟော်မုန်းနဲ့ သားအိမ်ကိုသန့်စေတဲ့ဟော်မုန်းက ဆေးတစ်မျိုး‌ထဲဖြစ်နေတတ်ပါတယ်။
                အဲဒီအခါမှာ နွားမရဲ့ ဟော်မုန်းကို မြိုင်လာအောင် ညှိယူလိုက်သလိုဖြစ်တဲ့အတွက် နွားမရဲ့
                ပုံမှန်အတက်ကျဖြစ်နေတဲ့ဟော်မုန်းတွေက ကမောက်ကမဖြစ်သွားပြီး မြိုင်စက်ဝန်းကို ပုံမှန် ပြန်လည်ပတ်ဖို့
                ခက်ခဲသွားနိုင်ပါတယ်။
                အဲဒီအခါကျ မြိုင်လာတာရော မလာတာရော အချိန်ကြာ‌တာတွေပါ
                ပိုပြီး ရှုပ်ထွေးသွားစေနိုင်ပါတယ်။
                အဲဒါကြောင့် အကြောင်းအမျိုးမျိုးဟာ အလကားဖြစ်လာတာမဟုတ်ပါ ပတ်သက်ဆက်နွှယ်နေတဲ့ နောက်ဆက်တွဲ ပြဿနာများစွာ
                ရှိလာနိုင်ပါတယ်။
                မြိုင်မလာတာကတော့ သားအိမ်မသန့်တာနဲ့လဲ သက်ဆိုင်ပါတယ်။</p>
            <p>* ဒီအကြောင်းကို ရေးသားမျှဝေတဲ့အခါမှာ မွေးမြူသူများ‌ရော၊စာဖတ်သူများပါ အလွယ်တကူ နားလည်စေဖို့ ရည်ရွယ်တဲ့အတွက်
