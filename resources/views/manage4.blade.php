                <p><strong>&nbsp;<span lang="hi-IN">Keeping yard records.</span></strong></p>
                <p>- <span lang="hi-IN">What to note</span>?</p>
                <p>- <span lang="hi-IN">Why</span>?</p>
                <p>&nbsp;<span lang="hi-IN">If I can measure that thing and remember that thing
                        Will manage well.</span></p>
                <p><span lang="hi-IN"><span style="color:#993300;">* What to note???</span></span></p>
                <p><strong><span lang="hi-IN">The main thing to remember that will make a profitable farm
                            points : </span></strong></p>
                <p>- <span lang="hi-IN">Number of cows milked throughout the year</span></p>
                <p>- <span lang="hi-IN">The daily milk yield of each cow</span></p>
                <p>- <span lang="hi-IN">Duration of lactation period </span>
                </p>
                <p>- <span lang="hi-IN">Average milk yield per cow of the dairy farm</span></p>
                <p>- <span lang="hi-IN">Ratio of labor to total cattle</span></p>
                <p>- <span lang="hi-IN">The length of time between births</span></p>
                <p>- <span lang="hi-IN">The number of times it takes a hybrid to get pregnant</span></p>
                <p>- <span lang="hi-IN">The age of the cow at the time of first calving</span></p>
                <p>- <span lang="hi-IN">The death rate of calf</span></p>
                <p>- <span lang="hi-IN">The number of young heifer is increasing year by year</span></p>
                <p>- <span lang="hi-IN">Food costs </span>(<span lang="hi-IN">What percentage of total manufacturing
                        cost?</span>)</p>
                <p><span lang="hi-IN"><span style="color: #993300">The 3 main types of records we must keep in order
                            to manage a profitable cattle farm are :</span>
                </p>
                <p><strong><span lang="hi-IN">1. Production </span></strong></p>
                <p>- <span lang="hi-IN">Number of cows milked throughout the year</span></p>
                <p>- <span lang="hi-IN">Daily milk yield of each cow</span></p>
                <p>- <span lang="hi-IN">A heifer's milk yield throughout the lactation period</span></p>
                <p>- <span lang="hi-IN">Duration of lactation period</span></p>
                <p>- <span lang="hi-IN">Average milk yield per cow of the farm</span></p>
                <p><strong>&nbsp;<span lang="hi-IN">2. Fertility and Animal Health</span></strong></p>
                <p>- <span lang="hi-IN">The length of time between births</span></p>
                <p>- <span lang="hi-IN">The age of the heifer at the time of first calving</span></p>
                <p>- <span lang="hi-IN">The number of heifers increasing year by year</span></p>
                <p>- <span lang="hi-IN">Immunization and treatment history</span></p>
                <p>- <span lang="hi-IN">Breeding history and stallion characteristics</span></p>
                <p>- <span lang="hi-IN">Growth rate of calves</span></p>
                <p>- <span lang="hi-IN">Body conditions of mature cows</span></p>
                <p><strong><span lang="hi-IN">3. Income and expenditure list</span></strong></p>
                <p>- <span lang="hi-IN">Ratio of labor to total cattle</span></p>
                <p>- <span lang="hi-IN">Total production cost per peittha of milk</span></p>
                <p>- <span lang="hi-IN">Food costs</span>(<span lang="hi-IN">What percentage of total production
                        costs?</span>)</p>
                <p>- <span lang="hi-IN">Profits</span></p>
                <p><span lang="hi-IN">The beginning of all records is the identification of the animal.</span></p>
                <p><span lang="hi-IN"><img
                            src="https://greenwaymyanmar.com/uploads/image/September2018/esTDzAEBEctKkq7oY6I53atUYj5CEH4hCRqAHMcp.png"
                            alt="" width="639" height="226"></span></p>
                <p><span lang="hi-IN">Then it's measurement.</span></p>
                <p><img src="https://greenwaymyanmar.com/uploads/image/September2018/qAcYF0fHRSSiCqIBcGzAzP4AgzEcuyDh1ZcmD6ns.png"
                        alt="" width="657" height="261"></p>
                <p><span lang="hi-IN">From measurements we can gauge whether there is deviation from our
                        directions.</span></p>
                <p><span lang="hi-IN">How much body weight gain per month?</span></p>
                <p><span lang="hi-IN"><img
                            src="https://greenwaymyanmar.com/uploads/image/September2018/4Muvj9UMyN8Uu1LK4LPUHjZofhwOWKR66gW212Nk.png"
                            alt=""></span></p>
                <p><span lang="hi-IN">How much milk do you get in one lactation period?</span></p>
                <p><span lang="hi-IN">Points are </span>:</p>
                <p>- <span lang="hi-IN">It should be practical and something that can actually be done.</span></p>
                <p>- <span lang="hi-IN">Set a time period and be able to do it during that time.
                    </span>(<span lang="hi-IN">For example </span>- <span lang="hi-IN">A milking period to increase
                        the
                        average milk yield of the whole farm by 10% per year.</span>)</p>
                <p>- <span lang="hi-IN">When measuring, it must be measured properly.</span></p>
                <p><span lang="hi-IN">So what should be included in setting the direction for the yard?</span>?</p>
                <p><strong><span lang="hi-IN">Fertility rate</span></strong></p>
                <p>- <span lang="hi-IN">The period of separation between children should be less than (14)
                        months.</span></p>
                <p>- <span lang="hi-IN">To get pregnant only 2 times.</span></p>
                <p>- <span lang="hi-IN">The heifer must be 24 months old when the first calf is born. At the time of
                        calving, the age of the cow must be (15) months.</span>
                </p>
                <p><span lang="hi-IN">The production of each</span></p>
                <p>- <span lang="hi-IN">To produce an average of 10 peittha per a day</span>(<span lang="hi-IN">၁၆
                        လီတာ</span>)</p>
                <p>- <span lang="hi-IN">To have a lactation period of 105 days.</span></p>
                <p>- <span lang="hi-IN">To get a total of (3050) pounds throughout the lactation period.</span>
                </p>
                <p>- <span lang="hi-IN">The number of lactating cows should not be less than 70% of the entire
                        herd.</span></p>
                <p><strong><span lang="hi-IN">Milk quality</span></strong></p>
                <p>- Total Plate Count &lt; 500,000 cfu/ml</p>
                <p>(<span lang="hi-IN">The number of bacteria in cow's milk must be less than 500,000
                        acceptable.</span>)</p>
                <p>- Somatic Cell Count &lt; 400,000</p>
                <p>( <span lang="hi-IN">Look at the cell count to see if you have mastitis</span>)</p>
                <p>- Total Solids &lt; 12.5%</p>
                <p>( <span lang="hi-IN">The total solid content must be above 12.5%.</span>)
                </p>
                <p><strong><span lang="hi-IN">Calf Growth and Branding</span></strong></p>
                <p>- <span lang="hi-IN">Calf rate to be less than 3% annually.</span></p>
                <p>- <span lang="hi-IN">For calves to weigh over 90 kg at weaning.</span></p>
                <p>- <span lang="hi-IN">The calf's weight gain should be above (600)grams per day.</span></p>
                <p>- <span lang="hi-IN">The bulls must meet the same criteria as the heifers။</span></p>
                <p>&nbsp;<span lang="hi-IN">Workers</span></p>
                <p>- <span lang="hi-IN">Labor turnover to be less than two percent a year.</span>
                </p>
                <p>- <span lang="hi-IN">To manage 15 milking cows per worker.</span></p>
                <p>- <span lang="hi-IN">To provide opportunities for workers to progress.</span></p>
                <p>- <span lang="hi-IN">To reward action.</span></p>
                <p>- <span lang="hi-IN">To provide labor training that will have a 100% impact on the farm
                        economy.</span></p>
                <p><span lang="hi-IN">Flow of income and expenditure</span></p>
                <p>- <span lang="hi-IN">To clearly define the cost of production.</span></p>
                <p>- <span lang="hi-IN">Total feed costs should be less than 60% of total farm costs.</span></p>
                <p>- <span lang="hi-IN">To note all costs.</span></p>
                <p>- <span lang="hi-IN">To make a specified profits.</span>(<span lang="hi-IN">An additional
                        profit of 300 kyats must be made for 1 pound of milk. 150 pounds per day = 45,000 kyat
                        profit</span>)
                </p>
