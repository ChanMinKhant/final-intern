                <p>&nbsp; &nbsp; တစ်ခါတစ်လေ ခြံသမားများတွင် ၎င်းတို့နွားများ နွားကလေးမွေးမည့်အချိန်ကို
                    စီစဉ်မထားတတ်ကြပါဘူး။ နွားမများမြိုင်လာတာတွေ့လျှင် သားတင်ပေးလိုက်သည်။ သို့ပေမယ့်
                    အချို့ခြံသမားများအတွက် ခြံရှိနွားအုပ်တစ်ခုလုံး၏ နွားကလေးမွေးမည့်အချိန်ကို ကြိုးစားပြီး
