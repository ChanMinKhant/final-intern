                    နွားကလေးမွေးပါတယ်။ တစ်နည်းအားဖြင့် ဒါဟာ ကျွန်ုပ်တို့ခြံတွင် နို့ထွက်နှုန်းအမြင့်ဆုံးရမည့်အချိန်နှင့်
