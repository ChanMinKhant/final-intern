                    နို့ပမာဏတစ်သတ်မတ်တည်းရဖို့အတွက် နွားအုပ်၏ သားစပ်ချိန်ကိုညှိရတာက နည်းစနစ်ကျကျ စီမံမှုတစ်ခုဖြစ်ပါတယ်။
