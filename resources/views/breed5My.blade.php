                    အနေထားမှန်အောင်ပြုလုပ်ပြီး ဆွဲထုတ်ခြင်းဖြင့် မွေးဖွားရ လွယ်ကူအောင် ဆောင်ရွက်ပေးနိုင်သည်။</p>
