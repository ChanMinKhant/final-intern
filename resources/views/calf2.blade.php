    <h3 class="page-title mt-3 " style="padding-top: 25px; margin-bottom: 40px;">
        <strong>Feeding the calves with milk bottles</strong>
    </h3>

    <div class="page-cover text-center" style="max-height: 400px; overflow: hidden; padding-bottom:30px;">
        <img src="https://greenway.sgp1.digitaloceanspaces.com/post_covers/fd365ddebf3407dbb4cf120a650f3f6b.jpg"
